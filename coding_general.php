<h1>Support for working in different languages</h1>


<!-- tre&#347;&#263; pomocy -->


<p>Spider significantly supports the creation of documents in various languages. Of course, the greatest emphasis is placed on HTML and its successor XHTML. However, support for other languages used in web development is also provided on an unprecedented scale.</p>

<p>Code support tools support languages to the greatest extent:</p>

<ul>
 <li><a href="coding_html.php">HTML and XHTML</a>, as well as other <a href="coding_markuplangs.php">markup languages</a> such as SMIL, WML, SVG, etc. ,</li>
 <li><a href="coding_css.php">Cascading Style Sheets (CSS)</a>,</li>
 <li><a href="coding_php.php">PHP (Hypertext Preprocessor)</a> - a server-side scripting language ,</li>
 <li><a href="coding_scripts_jsvb.php">JavaScript and VBScrpt</a> - client-side scripting languages (supported by a web browser),</li>
 <li><a href="coding_asp.php">ASP (Active Server Pages)</a> - Microsoft's server-side technology, primarily for Windows-based servers ,</li>
 <li><b>SQL</b> (database query language) in various versions<a href="coding_mysql.php">(MySQL</a>, PostgreSQL), and <a href="coding_mysql.php">support for MySQL dat</a> abases exists in the form of a number of additional tools .</li>
</ul>

<p>Spider also includes unparalleled support for creating <a href="tool_htmlhelp_general.php">HTML Help</a> files. The help document you are currently viewing was also created entirely in Spider.</p>


<p>Other languages are supported to a lesser extent:</p>
<ul>
	<li>Macromedia's<b>ColdFusion</b> (CFML),</li>
 <li><b>MathML</b>,</li>
 <li><b>Perl</b> (now increasingly displaced by PHP in web applications),</li>
 <li><b>Python</b>,</li>
 <li><b>Java</b>,</li>
 <li><b>C</b>, <b>C++</b>, <b>C#</b>,</li>
 <li>other.</li>
</ul>
<p>Supporting these languages primarily involves <a href="syntax.php">coloring their syntax</a>.</p>



