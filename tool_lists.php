<h1>Tools - Enumeration lists</h1>


<p>With the Enumeration Generator, you can easily create a numbered list, an unnumbered list, as well as a definition list.</p>

<p></p><p>To run this tool go to <a href="main_menu_main.php">The main</a> menu / <a href="main_menu_tools.php">Tools</a> / <a href="main_menu_tools_text.php">Text</a> / Enumeration list editor. You can also run this tool from the toolbar <a href="toolbars_link_format.php">Directors, text, formatting</a>.</p><p>The tool window will also open if you use the "Selected for numbered list" or "Selected for unnumbered list" command in the <a href="toolbars_link_format.php">Links, text, formatting</a> toolbar <a href="toolbars_link_format.php">,</a> or in the editor's context menu.</p>


<p>The tool window contains two tabs:</p>
<ul>
	<li><b>Enumeration list</b> - used to create a numbered (OL), or unnumbered (UL) list</li>
	<li><b>Definition list</b> - used to create a definition list (DL)</li>
</ul><a name="ol_ul"></a><h2>Enumeration list</h2>

<p>In this tab you can easily create a numbered or unnumbered list.</p>

<p>If you have selected several lines of text in the editor, they will be placed in the editor as list items (each text row will form a separate item).</p>

<p><b>Type</b><br>
Select the type of list (numbered or unnumbered).</p><p><b>Smaller spacing</b><br>
Check this option if you want less than standard spacing between lines. This setting has a <i>deprecated</i> status and it is not recommended to use it. It is recommended to use <a href="coding_css.php">CSS styles</a>.</p><p><b>Type</b><br><br>
Select a number type for a numbered list, or an identifier symbol for an unnumbered list. This setting has a <i>deprecated</i> status and it is not recommended to use it. It is recommended to use <a href="coding_css.php">CSS styles</a>.</p><p><b>Number from</b><br>
Select the starting number for the numbered list. This setting has a <i>deprecated (deprecated</i>) status and it is not recommended to use it. It is recommended to use <a href="coding_css.php">CSS styles</a>.</p><p><b>Element</b><br>
In this field you can enter and edit the content of the list element.</p><p>The buttons next to the list allow you to add, delete and sort (manually or automatically) items.</p>

<p><b>Automatically set the value to</b><br>
If you check this switch, the program will populate the VALUE attribute for each option with the next number in the list taking into account the value of the first field marked in the "Number FROM" field as long as it is a numbered list






<a name="dl"></a></p><h2>Definition list</h2>

<p>In this tab you can easily create a definition list. To do so, in the <b>Number of items</b> field (at the bottom of the tab), enter the number of list items, and then fill in the list by entering the <b>keyword on the left</b> and <b>its description on the right</b>.</p>



<h2>Element style</h2>

<p>This group allows you to specify the style for the tag. Here you will find three fields that allow you to specify:</p><p><b>Style definition</b> <br>
In this field you define the style for the element placed in the document. The button next to it launches the <a href="tool_style_editor.php">style editor </a> tool to create a style definition in a user-friendly and convenient way. By clicking there you can also modify an existing style.</p><p><b>Class selection</b><br>
In this field you specify the class identifier assigned to the tag. The drop-down list allows you to access classes defined in the current document and in external style sheets attached to it. The button next to it launches the <a href="tool_style_class.php">tool for creating a new class</a>.</p><p><b>Item ID</b><br>
Here you provide a unique identifier for this element in the document. These identifiers can be used, for example, when making jumps to specific parts of the page or in scripts.</p><h2>"Events and Attributes" button</h2>

<p>The "Events and Attributes" button allows you to open the <a href="tool_events_attributes.php">Events and Other At</a>tributes window. This window allows you to specify additional attributes of the edited tag that do not have their counterparts in the tool window. There you can also specify events for scripting languages that allow you to achieve various special effects.</p></td>
