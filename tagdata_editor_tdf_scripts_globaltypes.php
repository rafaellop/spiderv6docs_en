<h1>Global objects and types</h1>





<p>This window allows you to add a new global object of a specific type to the group of objects selected in the definition file settings for scripting. <a href="tagdata_editor_tdf_scripts.php">The definition file editor</a> allows you to create and modify an existing definition of the selected scripting language, which is used in the dynamic hints functions and on the <a href="main_tabs_languages.php">Programming Languages</a> tab.</p><p>The window consists of the following fields:</p><p><b>List of global objects and their types</b><br>
