<h1>Program settings - Additional tools</h1>

<!-- tre&#347;&#263; pomocy -->
<p>Spider allows you to run external programs via the View / Additional Tools menu or the button on the toolbar. You add the programs that are available on this list in this category of settings. Here are the settings available here:</p><h2>Additional tools</h2>

<p><b>List of additional tools</b><br>
This is a list showing an inventory of additional tools that can be quickly launched from the menu below the additional tools button and from the program's main menu. The tools can be launched with specific parameters, which you can specify below. Further tools can be added using the button next to them. You can also delete unwanted ones.</p><p><b>List operation buttons</b><br>
Using the buttons next to the program list, you can add more programs to the list, delete selected entries or modify them. You can also search the system for tools that are remembered in Spider. Once found, they will be added to the list.</p><h2>Tool start-up parameters</h2>

<p>In this group you can define startup parameters for the program selected on the list above</p><p><b>Tool start-up parameters</b><br>
You can assign launch parameters to the tool selected from the list above in this field. Using the adjacent buttons, you can insert variable parameters into this field, such as the path to the currently edited document or the current folder.</p><p><b>Parameter assignment buttons</b><br>
Using the buttons next to the field described above, you can assign special parameters, i.e. the path to the current document and the path to the folder in which the currently edited document is located. When you start the program, this variable parameter will be changed to an appropriate value depending on the path to the document being edited, and the program will receive this path as a parameter. The last two buttons allow you to assign the entered parameters to the program selected in the list or to delete the parameters assigned there.</p></td>
