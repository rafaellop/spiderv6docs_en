<h1>Tools - Chameleon - Renaming multiple files</h1>



<p>Chameleon provides invaluable assistance to webmasters who need to manage complex sites consisting of many component files. With the help of this tool, you can wholesale rename selected files located in designated folders. The program allows you to create your own renaming rules, with the help of which you can, for example, number all image files sequentially.</p><p>Chameleon has a user-friendly interface that takes you by the hand through every step of the renaming process, from selecting folders to preparing the list of files to be renamed, to setting up the conversion rules and the final process, which is quick and efficient.</p><h2>Launching</h2>

<p>You can run Chameleon using the command marked with the icon shown above located in the <a href="main_menu_main.php">Main Menu</a> / <a href="main_menu_file.php">File</a>, as well as via the pop-up menu of the file list on the <a href="main_tabs_myservices.php">Projects</a> tab. An alternative way to run the tool is to select several files in the list and press F2.</p><p>The first page of this tool is informative and welcomes you to the tool.</p><p>You can move between the pages using the Next and Back buttons.</p><h2>Folder selection</h2>

<p>The second page allows you to select the folders that contain the files you want to assign new names to. You add more folders using the buttons next to the list. Below the list is the <b>Include subfolders</b> switch, which causes the contents of subfolders indicated in the folder list to be taken into account when creating a list of files to be renamed.</p><h2>File selection</h2>

<p>The third page is created automatically based on the contents of the folders selected on the previous page. Additionally, you can add files here that are not included in the folders selected earlier. You can also remove selected files from the list. The list shows the file name, type, i.e. extension, last modified date and full path.</p><h2>Defining the replacement rules</h2>

<p>The next page allows you to set rules for renaming files. Rules are created in a separate pane described in the <a href="tool_chameleon_rules.php">Conversion Rule Editor</a> topic. Subsequent rules are assigned to the list. You can modify them, of course. You can also create different rules for different file types.</p><h2>Conversion plan</h2>

<p>After selecting the conversion rules and moving to the next page, a conversion plan is shown, that is, a report showing how the files will be named after the renaming. The list on this page displays in the following columns the full original path and post-conversion path, the original name and the post-conversion name. If a file is marked for deletion, this will be indicated in the Delete column. The list will not include files whose names, despite the rule, will not be changed.</p><p>The report can be saved to disk in HTML as well as TXT form, and some scheduled tasks can be deleted using a button marked with a minus sign.</p>

<p>Once you are sure of the changes that will be made, click the "Finish" button and the program will perform the conversion.</p></td>
