<h1>Highlighting scheme editor - Comments</h1>

<!-- tre&#347;&#263; pomocy -->Comments are intended to allow important information to be entered in the code to help the programmer see how the program works. This is particularly important when reading code written by another person. However, even your own code - after a long time - can be a puzzle.<p>Spider allows you to highlight comments in your code. Comments are denoted by special characters or strings - in each language these are different characters, e.g. '//' and the pair '/*' and '*/' in PHP, '&amp;lt;!--' and '--&amp;gt;' in HTML, etc.</p>

<p><b>Enable comment coloring</b><br>
Check this option if you wish to enable comment coloring.</p><ul>
<li><p><b>Single line comments</b><br>
Check this option if you wish to enable coloring of single-line comments.</p>
<p>Using the icons on the right-hand side of the window, you can add, delete and edit comment start marks. A different coloring style can be defined for each comment character.</p>
<p>Such coloring applies from the start of the comment (inclusive) to the end of the line in which it is placed. For example, in PHP this is the string '//'. Any characters can occur in a comment.</p></li>

<li><p><b>Multi-line comments</b><br>
Check this option if you wish to enable coloring of multi-line comments.</p>
<p>Using the icons on the right-hand side of the window, you can add, delete and edit comment start and end characters. A different coloring style can be defined for each comment character pair.</p>
<p>Such coloring applies from the start mark to the end mark of the comment (inclusive). The number of lines of code included in the comment does not matter. For example, this could be pairs of strings '/*, and '*/' in PHP, or '&amp;lt;!--' and '--&amp;gt;' in HTML. Any characters can occur in the comment, except for the character (string) marking the end of the comment.</p></li>
</ul>

