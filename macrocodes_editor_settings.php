<h1>Template Editor - Template Settings</h1>




<p><b>Template category</b><br>
Allows you to select the category in which the currently edited template will be placed. By default, it points to the category selected when the editor was launched.</p>

<p><b> Name </b> <br>
 Enter the name of the macro code in this field.</p>

<p><b>Template type</b><br>
Allows you to select the type of macro code (the default is "Simply Template"). This setting affects how the macro code is inserted and previewed (defined in the "Program Settings" window). It also defines the description under the small preview window, so that the user can see what the macro code contains.</p>

<p>The following types are available:</p>

<ul>
  <li><b>Just template</b> - plain template with an undefined type.</li>
  <li><b>JavaScript</b> - contains a JavaScript script</li>
  <li><b>JScript</b> - contains a script in the JScript language</li>
  <li><b>VBScript</b> - contains a script in VBScript language</li>
  <li><b>PHP</b> script - contains a script in PHP language</li>
  <li><b>Perl</b> script - contains a Perl language script</li>
  <li><b>Image</b> - contains the code for inserting an image</li>
  <li><b>Object</b> - contains object code</li>
  <li><b>Document template</b> - contains a document template. By default, using a macro code of this type automatically creates a new document (this can be changed in the <a href="settings_macrocodes.php">Template Settings</a> window).</li>
  <li><b>Special character</b> - contains a special character, such as an entity for letters of the Greek alphabet, etc.</li>
</ul>

<p><b>Keyboard shortcut</b><br>
Allows you to select a keyboard shortcut for a particular template. You can select a combination of Shift, Ctrl, Alt and one of the other alphanumeric and function keys.</p>

