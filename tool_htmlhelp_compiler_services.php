<h1>HTML Help - HTML Help and projects in Spider</h1>



<p>Since an HTML Help project is always a separate project, Spider allows you to treat such a project as a separate <a href="service_properites.php">project</a>. This allows you to apply to such a project all the mechanisms provided for projects, such as checking the correctness of syntax, conducting advanced search and replace, or opening the most frequently used files.<br><br>
Deciding whether an HTML project is also a project is done by creating a new project and selecting the appropriate option in its main window</p>

<h2>HTML Project Settings Help</h2>

<p><b>This project is also an HTML Help project</b><br>
Checking this option activates all project capabilities associated with the HTML Help project.</p>
<p><b>HTML Help (<tt>.hhp</tt>) main project file.</b><br>
In this window, enter the full path to the main project file along with the extension, or select from disk using the button next to the field.</p>
<p><b>Build Project </b> <br>
This button starts an immediate compilation of the selected project.</p>
<p><b>Compile this project quickly</b><br>
When this option is used, the dialog box for <a href="tool_htmlhelp_folder_to_chm.php">quickly compiling</a> the project to CHM form is launched.</p>
<p><b>Edit project</b><br>
This button launches <a href="tool_htmlhelp_hhp_editor.php">the project editor</a>, allowing you to make corrections or draft a project from scratch.</p>
<p><b>Browse compiled</b><br>
This button launches the already compiled CHM file for preview.</p>
<p><b>Compilation report</b><br>
When clicked, we call up the compilation report.</p>
<p><b>Run Help Workshop</b><br>
If you click on this button, the HTML Help Workshop program will open and the main project file will be loaded into it.</p>


