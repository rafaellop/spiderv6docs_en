<h1>Tools - "Embed" object</h1>

<!-- tre&#347;&#263; pomocy -->

<p>The window makes it easy to insert objects embedded in the document using the <tt>EMBED</tt> tag, such as AVI movies, MIDI files, etc. These objects will be played in the browser using the player (if installed on the system of the page viewer). This tag is marked as <i>deprecated</i> in the HTML specification.</p><p>The tool can be launched from the <a href="main_menu_main.php">Main Menu</a> / <a href="main_menu_tools.php">Tools</a> / <a href="main_menu_tools_links.php">Scripts and Objects</a> menu. The command icon is shown next to it.</p>

<p><b>Object</b> <br>
Enter the path to the object.</p>

<p><b>Object name</b> <br>
Enter the name under which the object will be identified in the document.</p>

<p><b>Alignment</b><br>
Select from the list how to align the embedded object to relative to other page elements.</p>

<p><b>Width</b><br>
Specify the width of the area designated on the page for displaying the item.</p>

<p><b>Height</b><br>
Specify the height of the area designated on the page for displaying the item.</p>

<p><b>Border</b> <br>
Enter the thickness of the border around the object on the page.</p>

<p><b>Volume</b><br>
If an object that plays sounds is inserted, specify the volume at which these sounds will be played.</p>

<p><b>Automatic start-up</b><br>
Check this option if you want the video, or audio playback to start automatically.</p>

<p><b>Reduced control panel</b><br>
Check this option if the control panel of the embedded object is to be displayed in a reduced form.</p>

<p><b>Hide the control panel</b><br>
Check this option if the control panel of the embedded object is not to be displayed.</p>

<p><b>Indefinitely</b><br>
Check this option if you want the sound or video to be displayed in an infinite loop.</p>



