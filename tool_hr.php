<h1>Tools - Horizontal line HR</h1>

<p>Tool is used to edit horizontal line tag settings HR. You can open them if the cursor is inside the HR using the <b> Edit tag in the tool ... </b> context menu command, or the <tt> F5 </tt> hotkey.</p>

<p>You can insert a horizontal line from <a href="main_menu_main.php">MainChamp;</a> <a href="main_menu_htmlcode.php">HTML Code</a> / <b>Horizontal Line Tag</b>, or by using the keyboard shortcut <tt>Ctrl+L</tt>.</p>

<p><b>Alignment</b><br>
Select from the list how the line should be aligned horizontally.</p><p><b>Line thickness</b><br>
Enter the thickness of the line (in points) in this field.</p><p><b>Line length</b><br>
Enter the length of the line in this field. You can also choose whether the specified value is:</p><ul>
	<li>percentages</li>
	<li>points</li>
</ul>

<p><b>Line color</b><br>
Click to open <a href="tool_colorpalette.php">the color palette</a> and select <a href="tool_colorpalette.php">a</a> line color.</p><p><b>Insert line appearance settings as a style</b><br>
Check this switch to insert the above settings into the markup not as individual attributes, but as a CSS style in the <tt>style</tt> attribute.</p><h2>"Events and Attributes" button</h2>

<p>The "Events and Attributes" button allows you to open the <a href="tool_events_attributes.php">Events and Other At</a>tributes window. This window allows you to specify additional attributes of the edited tag that do not have their counterparts in the tool window. There you can also specify events for scripting languages that allow you to achieve various special effects.</p><h2>Element style</h2>

<p>This group allows you to specify the style for the tag. Here you will find three fields that allow you to specify:</p><p><b>Definition of style</b><br>
In this field you define the style for the element to be placed in the document. The button next to it launches the <a href="tool_style_editor.php">style editor</a> tool that allows you to create a style definition in a friendly and convenient way. By clicking there you can also modify an existing style.</p><p><b>Class selection</b><br>
In this field you specify the class identifier assigned to the tag. The drop-down list allows you to access classes defined in the current document and in external style sheets attached to it. The button next to it launches the <a href="tool_style_class.php">tool for creating a new class</a>.</p><p><b>Item ID</b><br>
Here you provide a unique identifier for this element in the document. These identifiers can be used, for example, when making jumps to specific parts of the page or in scripts.</p></td>
