<h1>Tools - Color palette</h1>

<p>The color palette is a tool that allows you to easily select and place colors in your document or select colors in tool boxes. The palette also allows you to create many different palettes that you use to create your project.</p><p>The color palette can be launched in several ways. First of all, from the mini toolbar next to the editor. In addition, using the keyboard shortcut Shift+Ctrl+C and from the <a href="main_menu_main.php">Main Menu</a> / <a href="main_menu_tools.php">Tools</a> / <a href="main_menu_tools_other.php">Other Tools</a> / Color Palette. As already mentioned, the palette window is also displayed when selecting a color in toolboxes of various types. In all cases, the command that launches the color palette is indicated by the icon shown above</p><p>The palette, once opened via the main menu or a button on the mini toolbar, is dockable within the main window. For example, you can dock it on the right side of the screen and have it in view at all times.</p>

<h2>Using the color palette</h2>

<p>The main advantage of this tool is that you can define a number of different palettes consisting of any color and use them easily and quickly. A palette can consist of a theoretically unlimited number of colors. The palette-based solution allows you to maintain color consistency of the created Web pages because the same colors can be used in any tool and at any time.</p><p>After installing the program, several basic palettes are available, which can be changed using the list at the top of the window. The buttons below the list allow you to manage the palettes, i.e.</p><ul>
	<li>creating a new palette</li><li>delete a selected palette</li><li>saving a palette</li><li>importing a palette</li><li>view this help topic</li></ul>

<p>To create a new palette, press the create new palette button, give it a name in the window that appears, and then define the component colors. Once a new palette is created, it consists of only 16 items that do not contain any colors. To define the colors, use the buttons at the very bottom of the window. These buttons are described further on.</p><p>Once you have created your own palette, please save it using the button marked floppy.</p><p>The palette tool also features a very useful function for importing palettes created in other programs. You can import palettes from HomeSite (tested on version 4.01) and Paint Shop (tested on versions 4.x through 7.04). It is extremely useful to import a palette from Paint Shop. This is because you can export a palette created from a selected graphics file in PSP and import it into Spider. This will preserve the highlighting scheme not only within the texts posted on the Web, but also within the graphics.</p><p>Below the palette's color grid are buttons for operations on the selected palette, i.e.</p><ul>
	<li>selecting a color from outside the palette using the color selection window,</li><li>selecting a color by specifying its value with a hexadecimal code in the form of <tt>#FF7348</tt>, or <tt>FF7348</tt> (without the cross character <tt>#</tt>),</li><li>Add the selected color to the palette (color grid).</li><li>Remove a color selected in the grid from the palette.</li><li>Replacing the selected color on the grid with the selected color.</li><li>Selecting a color from the screen (pipette) - with this button you can select a color from anywhere on the screen. You move the mouse across the screen, and the palette window shows the color that is under the mouse. To finish the selection you press the keyboard shortcut set in the <a href="settings_general.php">general settings</a> window.</li><li>Inserting a color into a document or tool. Selecting any text when it is not a color code (e.g., #0000ff or red) in the editor and selecting the "Insert selected color" command on the dockable color palette causes the selected text to be covered by a color (or more precisely, a <tt>div</tt> tag with the appropriate color set). When the selected text is color-coded, it is converted to the color inserted from the color palette.</li></ul>

