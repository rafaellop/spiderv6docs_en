<h1>Tools - Embedding multimedia</h1>


<!-- tre&#347;&#263; pomocy -->

<p>The window makes it easy to insert multimedia into a document, such as Flash videos, Realmedia, Quicktime.</p><p>The tool can be launched from the menu <a href="main_menu_main.php">The main menu</a> / <a href="main_menu_tools.php">Tools</a> / <a href="main_menu_tools_gfx.php">Graphics &amp; Multimedia</a>, as well as from the toolbar <a href="toolbars_gfx.php">Graphics and Media</a>. The command icon is shown next to it.</p>
<p>An alternative way to launch the tool is by dragging a media file into the perimeter of a document open in the editor.</p><b>Type of media to be embedded</b><br>
Select the type of embedded file:<ul>
	<li>Macromedia Flash ™</li>
	<li>Macromedia Shockwave ™</li>
	<li>Realmedia ™</li>
	<li>Apple Quicktime ™</li>
</ul>

<p><b>Path to media file </b> <br>
Enter the path to the file with the presentation or movie. You can use the window opened with the button next to the field.</p><b>Method of embedding</b><br>
Choose how you want the presentation or movie to be embedded. You can choose to insert using:<ul>
	<li><b>Object (ActiveX)</b> - a technology supported by Internet Explorer</li>
	<li><b>Object (ActiveX) + Embed </b> - to insert both an ActiveX control and an Embed object (for Netscape). This is the recommended setting.</li>
	<li><b>Embed</b> (required by Netscape)</li>
</ul>

<h2>Presentation parameters</h2>

<p><b>Height</b>, <b>Width</b><br>
Enter the dimensions of the movie you want to insert.</p>

<p><b>Name</b><br>
Enter a name for the object to be inserted.</p>

<p><b>identifier</b><br>
Type the ID (<tt>id</tt>) of the object you are inserting</p>


<h2>Additional parameters</h2>
<p>The list includes all parameters passed to the media player. You can change the default data, such as background color, number of times a movie is played, etc. The list of parameters changes with the change of the presentation type selected at the top of the window.</p>





<h2>"Events and Attributes" button</h2>

<p>The "Events and Attributes" button allows you to open the <a href="tool_events_attributes.php">Events and Other At</a>tributes window. This window allows you to specify additional attributes of the edited tag that do not have their counterparts in the tool window. There you can also specify events for scripting languages that allow you to achieve various special effects.</p></td>
