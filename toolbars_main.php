<h1>Toolbars - Main toolbar</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>This toolbar contains frequently used commands to operate the program.</p>

<p><b><a href="main_menu_file_new.php">New document</a></b><br>
Click to create a new, blank document. By clicking on the arrow on the right, you can use the submenu to launch additional tools.</p>


<p><b><a href="tool_importer.php">Import...</a></b><br>
Click to open the File importer.</p>

<p><b>Open</b><br>
Click to open an existing document. Click on the arrow on the right to access the list of recently opened documents.</p>

<p><b>Save</b><br>
Click to save the current document.</p>

<p><b>Print...</b><br>
Click to open the print window.</p><p><b>Undo</b><br>
Click to undo the last action in the editor (the default shortcut <tt>Ctrl+Z</tt>).</p><p><b>Redo</b><br>
Click to perform the undo operation in the editor again (default shortcut <tt>Shift+Ctrl+Z</tt>).</p><p><b>Cut</b><br>
Click to cut the selection to the clipboard (the default shortcut <tt>Ctrl+X</tt>).</p><p><b>Copy</b><br>Click to copy the selection to the clipboard (default shortcut <tt>Ctrl+C</tt>).</p><p><b>Paste</b><br>
Click to paste the contents of the clipboard (default shortcut <tt>Ctrl+V</tt>).</p><p><b>Resource panel on the left</b><br>
A submenu containing switches to activate one of the tabs of the <a href="main_tabs_general.php#tabs_leftpanel">panel at the left</a> in the main program window. The default keyboard shortcut is <tt>F9</tt></p><p><b>Full screen</b><br>
This command enables or disables full-screen mode, during which only the editing or preview area, the mini-bar and the menu are visible. The default keyboard shortcut is <tt>Shift+F9</tt>.</p><p><b>Resources panel on the right</b><br>
A submenu containing switches to activate one of the tabs of the <a href="main_tabs_general.php#tabs_rightpanel">panel on the right</a> in the main program window. The default keyboard shortcut is <tt>Ctrl+F9</tt></p><p><b>Program settings</b><br>
Click to change the current program settings opening the <a href="settings.php">program settings</a> window. The submenu available by clicking the arrow develops a menu of commands for settings (identical to <a href="main_menu_view_settings.php">Main Menu / Display / Settings</a>).</p><p><b>Document preview</b><br>
Click this button to preview the current document in the default viewer. You can define the default browser in the <a href="settings_browse.php">Browsing Settings</a>.If you click the arrow next to the button, you will be able to select additional browsers from the menu, which you also define in the aforementioned settings window. This will allow you to bring up a preview of the current document in browsers other than the one selected by default. The menu under the button is identical to the <a href="main_menu_main.php">Main menu</a> / <a href="main_menu_view.php">View</a> / Preview.</p></td>
