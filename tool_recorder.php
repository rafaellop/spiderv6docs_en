<h1>Tools - Activity recorder</h1>



<!-- tre&#347;&#263; pomocy -->
<p>You will start the Activity Recorder from the <a href="main_menu_main.php">Main Menu</a> / <a href="main_menu_tools.php">Tools</a> / <a href="main_menu_tools_other.php">Other Tools</a> / Activity Recorder, as well as from the <a href="main_menu_view.php">Display Menu</a>. the tool icon is shown above.</p>

<p>The tool allows you to record activities for later reuse. You can record not only activities in Spider, but also in the system. However, be careful that the tool plays back the recording under the same conditions under which it was recorded. Otherwise, errors may occur.</p>

<p>You can un-dock the tool window and move it to another place in the document by dragging it by the bar at the top of the window.</p>

<p>The following commands are available in the tools menu:</p>

<p>- click to play the recording.</p>

<p>-click to start recording.</p>

<p>- click to stop recording.</p>

<p>- click to open a recording from the list.</p>

<p>- click to save the recording on the list.</p>

<p>- click to remove a recording from the list.</p>

<p>- click, create a new folder for recordings.</p>

<p><b>Name</b><br>
Displays the name of the current recording.</p>

<p>Use the slider under the menu to specify the playback speed. The smaller the number, the faster the playback will be. A value of <tt>100</tt> indicates normal speed.</p>

