<p><b>Converting text into an enumerated list</b><br></p><p>To convert a piece of text into an enumerated list (either numbered or unnumbered):</p>

<ol>
   <li>Select a text fragment in the editor. In the selected text, each new line will be a new item in the enumeration.</li>
   <li>From the editor pop-up menu, choose "Selected text to ... -&amp;gt; Selected to numbered list" or "Selected text to ... -&amp;gt; Selected to unnumbered list", depending on the type of list you want to create.</li>
   <li>In the enumeration window that appears on the screen, specify the type and other parameters of the list. You can also modify its elements.</li>
</ol>

<p>Clicking OK replaces the selected text with an enumeration based on it.</p>



<h2>Commands for inserting and editing lists tags</h2>If you quickly want to insert a numbered list tag <tt>OL</tt>, an unnumbered list tag <tt>UL</tt>, a list element tag <tt>LI</tt>, or a definition list skeleton <tt>DL</tt>, you can use the commands in the <a href="main_menu_main.php">main menu</a> / <a href="main_menu_htmlcode.php">HTML code</a> / <a href="main_menu_htmlcode_lists.php">Enumerations</a>, or in the <a href="toolbars_link_format.php">toolbar Links, text and formatting</a>.<p>Once the list has been created, you can edit individual tags. You can do this in the tag editing window. Simply place the cursor on the desired tag and press the <tt>F5</tt> key.</p>

