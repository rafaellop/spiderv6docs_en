<h1>Program settings - Source code formatting</h1>



<!-- tre&#347;&#263; pomocy -->
<p>This category of settings allows you to specify <a href="formatter_general.php"> source code formatting </a> settings for different types of documents. Thanks to this function, you can quickly organize your code in a clear way.</p><p>For markup language formatting, CSS and for Tidy, you can create predefined settings, the so-called profiles, which allow you to specify different ways of formatting. Profiles cannot be created for PHP and JavaScript formatting and only one setting is available.</p><p>The category is divided into several groups depending on the formatting they apply to:</p><h2>Predefined tag formatting settings</h2>

<p>In this group there is a list of formatting profiles for markup languages (e.g. HTML), which you can customize to your needs using the <a href="formatter_markuplangs.php"> Formatting Settings </a> window by one of the buttons next to it letters. You can also create formatting from scratch.</p><h2>Predefined CSS formatting settings</h2>

<p>This group lists the formatting profiles for CSS stylesheets, which you can customize using the <a href="formatter_stylesheets.php"> CSS Formatting Settings </a> window by one of the buttons next to the list. You can also create formatting from scratch.</p><h2>Predefined Tidy(r) formatting settings</h2>

<p>This group lists the formatting profiles for <a href="https://www.w3.org/People/Raggett/tidy/" target="_blank">Tidy (r)</a> which are you can customize it with the <a href="formatter_tidy.php">Formatting Settings for Tidy (r)</a> window by one of the buttons next to the list. You can also create formatting from scratch.</p><h2>Formatting settings for script languages</h2>

<p>In this group of formatting settings there are two buttons that launch the source code formatting settings panes for JavaScript ( <a href="formatter_javascript.php">JavaScript Settings</a> ) and PHP ( <a href="formatter_php.php">PHP Settings</a> ).</p></td>
