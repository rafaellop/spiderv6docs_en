<h1>Main menu - Edit/Paste from clipboard with replacement...</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>This menu allows you to paste text from the clipboard, while performing the selected operation. The following commands are available:</p>


<p><b>Insert and replace &amp;lt; and &amp;gt; with &amp;lt; and &amp;gt;</b> <br>
Click to paste the text from the clipboard, replacing the &amp;lt; and &amp;gt; characters with entities &amp;lt; and &amp;gt;. This makes it possible to display them in the document, most often they are used to present HTML tags on the page that you do not want to interpret.</p><p><b>Replace &amp;lt; and &amp;gt; with &amp;lt; and &amp;gt; in the selected</b><br>
Click to paste text from clipboard, replacing entities &amp;lt; and &amp;gt; with &amp;lt; and &amp;gt; characters.</p><p><b>Insert and replace with a numbered list</b><br>
Click to paste text from the clipboard, turning it into a numbered list(<tt>OL</tt>). Each line of the selected text will be a new list item.</p><p><b>Insert and replace with an unnumbered list</b> <br>
Click to paste the text from the clipboard, replacing it with an unnumbered list (<tt>UL</tt>). Each line of selected text will be a new item in the list.</p><p><b>Paste and replace with a comment</b><br>
 Click to paste the text from the clipboard, replacing it with an HTML comment.</p><p><b>Insert and preserve layout</b><br>
Click to insert text from the clipboard preserving its layout (text covered by the <tt>PRE</tt> tag).</p><p><b>Insert with indentation</b><br>
Click to insert text from the clipboard while preserving indentation. This means that if the line where you paste text is indented, the same indent will be inserted at the beginning of each new line of pasted text.</p></td>
