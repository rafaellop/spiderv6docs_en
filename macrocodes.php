<h1>Templates - in general</h1>




<!-- tre&#347;&#263; pomocy -->

<p>Templates are used to quickly insert frequently used code snippets, tags, and even generate complete documents.</p>

<p>Spider provides hundreds of templates, such as "Code Templates" (the most commonly used constructs in various scripting languages), "Page Templates" (templates that generate complete pages including graphics!), "Special Characters" (letter entities of various alphabets, etc.) and many more. All this to make working with Spider as efficient as possible!</p>

<p>Individual templates are grouped into categories on a tree that takes up most of the templates tab located in the <a href="main_tabs_general.php#tabs_leftpanel">panel on the left</a>.</p>

<p>It is also possible to display the templates tab directly - use the shortcut Ctrl+4. If the <a href="main_tabs_general.php#tabs_leftpanel">panel on the left</a> was not visible, instead of the tab you will see a docked window that can be placed in any part of the screen.</p>

<p>The capabilities of templates have been significantly extended compared to their predecessors from Spider 2000. It is even suggested by the name change - they are used not only to insert tags.</p>

<p>It is possible to build templates containing <a href="main_tabs_macrocodes.php#variables">variables</a> and <a href="main_tabs_macrocodes.php#blocks">blocks</a> that allow the template user to adjust the inserted code to the current needs, without modifying the template itself!</p>

<p>With the help of templates, you can create complete documents, including graphics, attached documents, etc.</p>

<p>When creating a template, you can specify its type (JavaScript, Object, Image and others). Different template types behave slightly differently when previewing and inserting. For details, see the help to the<a href="macrocodes_editor.php" title="Edytor szablonÃ³w">"Template Editor</a>" window.</p>



