<p>In Spider, due to the large number of settings that can be changed by the user, it was necessary to divide the settings window into sub-windows that contain subsequent categories of settings. The window described in this chapter is related to the website editor. You will find here both general and very specific settings of the editor itself, as well as, for example, spell checking tools or coloring scheme settings.</p><p>The following groups of settings are available within this window:</p><ul><a name="settingslisteditor"></a><li><a href="settings_editor_general.php">General</a> - settings related to the general operation of the editor program and some default options.</li><li><a href="settings_editor_font.php">Editor font</a> - related to the font used in the editor.</li><li><a href="settings_editor_conf.php">Configuration</a> - many options related to the specific operation of the code editor.</li><li><a href="settings_editor_spelling.php">Spelling and syntax verification</a> - spellchecking and syntax check options.</li><li><a href="settings_editor_syntax.php">Syntax coloring</a> - a list of coloring schemes with buttons that allow you to modify them as well as create new ones.</li><li><a href="settings_editor_documents.php">Documents</a> - settings related to working on documents, e.g. settings for converting national characters or settings remembered with documents.</li><li><a href="settings_editor_wysiwyg.php">Visual editing</a> - action-related visual editing tool in visual mode.</li></ul>

