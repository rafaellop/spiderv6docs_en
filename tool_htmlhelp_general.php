<h1>HTML Help - General Information</h1>



<p>HTML Help is a universal format for creating help in the form of uniform files with CHM extension. The creation of help in this format is available to anyone who can create HTML pages, and has HTML Help Workshop or another program with a CHM compiler installed on the system. <br><br>
HTML Help is nothing more than a set of HTML pages along with style sheets, scripts and graphics compiled and compressed into a single CHM file, which retains all the richness of forms that the HTML format has. <br><br>
Since HTML Help has built-in ability to communicate with programs, it is mainly used to create help for these programs, and because of its ease of execution, it is commonly used for this purpose. However, its ease of construction and presentation capabilities are also used for purposes other than program help. The built-in indexing mechanism and a very good search mechanism make it possible to create excellent databases. By retaining all the features of HTML, it can be used to collect Internet addresses, e-mail - all with the possibility of description or inclusion of graphics. You can also use this format to present albums, graphics, as well as virtual magazines and books.<br><br></p><h2>Creating CHM in Spider</h2>

<p>Creating HTML Help in Spider has been simplified as much as possible and reduced to knowing how to use a few tools:</p>

<p><b>Project editor</b><br><a href="tool_htmlhelp_hhp_editor.php">The project editor</a> allows you to set all the parameters: what files to include, general settings of the overall project - appearance, sizes, behavior, window properties, and HTML Help Api, i.e. communication with files and programs.</p>

<p><b>Table of contents editor</b><br><a href="tool_htmlhelp_hhc_editor.php">The editor</a> is used to create the table of contents - the main navigation part of the help file. It allows you to assign each item a specific page, which is then displayed in the main window. The table of contents is drawn up as a hierarchical tree of icons and names. The editor also allows you to customize the table of contents in terms of appearance.</p>

<p><b>Index Editor</b><br><a href="tool_htmlhelp_hhk_editor.php">The index editor</a> allows you to create an index superbly facilitating navigation through the whole thing. Individual index items are references to specific places in either the current or external project. You can also group specific topics, or call up dialog boxes containing groups of topics.</p>

