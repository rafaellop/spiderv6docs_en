<h1>Main menu - Projects</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>The commands in this menu are used to work with files and folders on the local disk. You can also use this menu to create <a href="services_general.php">projects</a> and switch between them.</p>

<p>The commands and menu layout are identical to the <a href="main_tabs_myservices.php">Projects</a> tab. The following commands are available:</p>



<p><b><a href="service_properites.php">Project properties</a></b><br>
Click to open the project properties window. You can also use the <a href="main_menu_services_properties.php">submenus</a> to use additional options.</p><hr>

<p><b>New project</b><br>
Click to create a new project.</p><p><b>Remove project</b><br>
Click to delete the currentproject. Deleting a project will only delete information about that project from Spider. No files will be deleted from your hard drive.</p><p><b>Rename project</b><br>
Click to change the name of the current project.</p><hr>

<p><b>Add to the project</b><br>
Click to select and copy a component (file or folder) to the current project. You will be able to select where in the project the component should be copied to.</p><p><b>Remove project item</b><br>
Click to remove the selected component (file or folder) from the currentproject.<br>NOTE: The item will be removed from the disk and moved to the Recycle Bin. </p><hr>

<p><b><a href="service_restore.php">Restore project</a></b><br>
Click to launch the Project Restore tool.</p><p><b><a href="services_general.php">Project help</a></b><br>
Click to open the help.</p><hr>

<p><b>Expand subfolders</b><br>
Click to expand all subfolders of the folder selected in the tree in the Projects tab.</p><p><b>Collapse subfolders</b><br>
Click to collapse all subfolders of the folder selected in the treein the Projects tab.</p></td>
