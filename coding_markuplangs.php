<h1>Support for markup languages</h1>



<!-- tre&#347;&#263; pomocy -->

<p>HTML (and its successor, XHTML) is the most important markup language used to create pages. However, there are many more markup languages besides it. Spider supports the most important (in terms of Web usage) of them :</p><ul>                                                                                                                                                                                                                                   
	<li><b>WML</b> - ( <i>Wireless Markup Language</i>) is a language for describing pages used by mobile devices (mainly cellular phones) via WAP ( <i>Wireless Application Protocol</i>)</li>
	<li><b>SVG</b> - ( <i>Scalable Vector Graphics</i>) - an alternative to Flash animation</li>
	<li><b>MathML</b> - ( <i>Mathematical Markup Language</i>) - a language for representing mathematical symbols and expressions</li>
	<li><b>SMIL</b> - ( <i>Synchronized Multimedia Integration Language</i>) - a language supporting the creation of multimedia presentations</li>
	<li><b>XML</b> - ( <i>eXtensible Markup Language</i>) - a universal, increasingly popular document description language</li>
	<li><b>RSS</b> - ( <i>Really Simple Syndication 2.0</i>) - a language for distributing news, etc.</li> 
	<li><b>P3P</b> - (<i>Platform for Privacy Preferences 1.0</i>)</li>
   <li><b>Atom</b> - a new way to distribute news and other information on the Web.</li>
</ul>

<p>Information about the language, its tags, attributes, etc. Spider takes from special definition files for markup languages - TDF. To edit the data, there is a special tool - Markup <a href="tagdata_editor_tdf.php">Language Definition File Editor</a>. Using it, you can also add and remove supported markup languages. This makes Spider a versatile and very flexible editor, perfect for creating documents in any markup language.</p>

<p>Because Spider is intended primarily for web development, everything related to HTML only is covered in the <a href="coding_html.php">HTML Support</a> topic.</p>

<p>However, everything you will read about in this help topic applies to HTML and XHTML as well as the others mentioned above.</p>

<p>Support for markup languages primarily includes tools:</p><ul>
	<li><a href="coding_markuplangs_syntax.php">Syntax highlighting</a></li>
	<li><a href="main_tabs_navigator.php#html">Tag navigator</a></li>
	<li><a href="coding_markuplangs_codeinsight.php">Automatic tag completion, Dynamic code, Dynamic tooltips</a></li>
	<li><a href="coding_markuplangs_formatting.php">Source code formatting</a></li>
</ul>




