<h1>Image viewer</h1>

<!-- tre&#347;&#263; pomocy -->


<p>The Image viewer allows you to select and preview the image you want to use for insertion into your document. The tool can be called from editing windows that support inserting graphics, e.g. <a href="tool_graphic_insert.php">Insert Graph</a>ics, check boxes for graphic elements (such as a background image) in the <a href="main_tabs_inspector_css.php">CSS Inspector</a>, <a href="tool_link_map_editor.php">Links Map Editor</a>.</p>The window consists of three parts:<ul>
	<li>Button bar</li>
	<li>Folder and file lists</li>
	<li>Preview window</li>
</ul>

<p>The button bar is used to switch the view of the file list and to select the preview type. The following commands are available:</p>

<ul>
	<li><b>Select a project to browse</b> - click to quickly move to the folder of the selected <a href="main_tabs_myservices.php">Project,</a></li>
	<li><b>Large Icons</b>, <b>Small Icons</b>, <b>List</b>, <b>Details</b> - click one of the buttons to choose how the files are displayed in the list below,</li>
	<li><b>Stretch the image to the dimensions of the area</b> - click to enlarge the selected image to the size of the preview area,</li>
	<li><b>Simulate background</b> - click to have the image repeated multiple times to form the background of the preview area. If you choose this option, the preview area will display sample text, and boxes will appear next to the buttons that allow you to change the content and color of the text.</li>
</ul>




<p><b>Preview area and status bar</b><br>
The preview area simply acts as a place where the viewed files are displayed. The status bar, on the other hand, displays the file size, dimensions and file format of the image.</p>

