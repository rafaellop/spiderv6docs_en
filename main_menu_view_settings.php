<h1>Main menu - View - Settings</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>With this menu you can activate various program settings:</p>

<p><b>Editor options</b><br>
Sub-menu containing the switches that control the editor. The content of this menu is similar to that of the editor's <a href="toolbars_minitoolbar.php">mini toolbar</a>.</p><hr>

<p><b>General program settings</b><br>
Displays the <a href="settings.php">program settings</a> window. Default keyboard shortcut <tt>F8</tt>.</p><hr>

<p><b>Customise editor, templates, spelling</b><br>
Displays the editor <a href="settings_editor.php">settings</a> window.</p><p><b>Customise toolbars, menus and shortcuts</b><br>
Displays the <a href="settings_toolbars.php">settings</a> window for <a href="settings_toolbars.php">toolbars, menus and</a> shortcuts.</p><p><b>Customize facilities and completion</b><br>
Displays the <a href="settings_codeinsight.php">facilitation and completion settings</a> dialog.</p><p><b>Customize your network and servers</b><br>
Displays the <a href="settings_network.php">settings of networks and FTP servers</a>.</p><p><b>Customise additional tools, Tidy and HTML Help</b><br>
Displays the <a href="settings_addtools.php">settings</a> window for <a href="settings_addtools.php">additional tools, Tidy and HTML Help</a>.</p><hr>

<p><b>Editor of TDF definition files</b><br>
Displays <a href="tagdata_editor_tdf.php">the markup language definition file (TDF) editor</a> window.</p><p><b>PHP definition file editor</b><br>
Displays <a href="tagdata_editor_tdf_php.php">the PHP language definition file editor</a> window.</p><p><b>CSS definition file editor</b><br>
Displays the CSS definition <a href="tagdata_editor_tdf_css.php">file editor</a> window.</p><p><b>Editor of script definition files</b><br>
Displays the script definition <a href="tagdata_editor_tdf_scripts.php">file editor</a> window.</p><p><b>Highlighting scheme editor</b><br>
Displays the <a href="syntax_editor.php">syntax scheme editor</a> window for the documents being edited.</p></td>
