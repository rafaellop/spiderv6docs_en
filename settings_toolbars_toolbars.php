<h1>Program settings - Toolbars configuration</h1>





<!-- tre&#347;&#263; pomocy -->
<p>This category includes tools that allow you to modify the contents of the toolbars. This category consists of two lists presenting the list of all available "Toolbars" toolbars and the contents of the toolbar selected in the first "Toolbar content configuration" list. Using the buttons next to the lists, you can change the name of the toolbar displayed after it is undocked, and also modify the content of the bars. At the very bottom of this category, a description of the selected item is displayed on the toolbar.</p><p>Toolbar settings are remembered between program startups, both in terms of the content of the toolbars and their arrangement on the screen.</p><h2>Example of changing the toolbar configuration</h2>

<p>To change the content of a selected toolbar, eg delete selected items or add new ones, select this toolbar in the list at the top (eg "User 1" bar). After selecting it, the content of the selected toolbar will appear on the list below. In the case of the user toolbar, which is initially empty, there will of course be no items. To add an item to the toolbar, click the button marked with a plus icon. The <a href="settings_toolbars_toolbars_addnew.php"> Item </a> window will appear (click for a description) where you can specify the parameters of the item being added. You can add three different items to the selected toolbar:</p><ul>
	<li><b>Action-related button</b> - this is a simple button that, when clicked, triggers the selected action.</li><li><b>A button associated with an action and a submenu</b> - this is a button that can both be clicked and, when clicked, the corresponding action will be triggered. This button can also have an additional menu available by clicking on the right-hand side of the button.</li><li><b>Button linked to a submenu only</b> - this button, when clicked, displays the associated menu from which further options are then selected</li><li><b>Element separator</b> - this is the element that separates the buttons on the toolbar.</li></ul>

<p>After accepting the new element, it will appear on the list, and the appropriate icon will be displayed next to it.</p><p>You can change the order of items on the selected toolbar. The buttons marked with arrows are used for this. You can also change the selected item by assigning it a different action or submenu.</p></td>
