<h1>Program settings - Visual editing</h1>

<!-- tre&#347;&#263; pomocy -->
<p>This category allows you to specify basic visual editing settings, that is, in graphical mode and not with the source code view. There are two groups of settings here where you have an influence on the visual editing function via the settings:</p><p><b>Edit newly opened documents in visual mode</b><br>
Check this option to edit newly opened documents in visual mode instead of in the code editor.</p><p><b>Show icons to replace some invisible tags</b><br>
Check this option to have some invisible tags represented with special icons when editing in visual mode.</p><p><b>Display invisible table borders</b><br>
Check this option to make table borders visible in visual editing mode, which will be invisible when viewing.</p><p><b>Cleaning up the source code</b><br>
Check this option to have the source code of the document cleaned up with Tidy when switching from editing in visual mode to working with the source code.</p>

