<h1>Program settings - Toolbar item</h1>





<!-- tre&#347;&#263; pomocy -->
<p>This window is launched from the toolbar <a href="settings_toolbars_toolbars.php">modification</a> tools and allows you to add an element to the selected toolbar that is compatible with the settings selected in this window. You can add an element to the selected toolbar that is one of the following:</p><ul>
	<li><b>Action-related button</b> - this is a simple button that, when clicked, triggers the selected action.</li><li><b>A button associated with an action and a submenu</b> - this is a button that can both be clicked and, when clicked, the corresponding action will be triggered. This button can also have an additional menu available by clicking on the right-hand side of the button.</li><li><b>Button linked to a submenu only</b> - this button, when clicked, displays the associated menu from which further options are then selected</li><li><b>Element separator</b> - this is the element that separates the buttons on the toolbar.</li></ul>

<p>Depending on the selected element type that you want to add, additional selection lists and elements that you can use are activated in the window. There are two lists "Assigned Action" and "Assigned Submenus".</p><h2>Assigned action</h2>

<p>This list is activated for the first two types of toolbar item. Its content is a list of all commands (actions) that you can assign to the element being created. Just select an action from the list and it will be assigned.</p><h2>Assigned submenu</h2>

<p>This list is activated for the second and third type of toolbars and allows you to associate an item added to a toolbar with the selected submenu. The list of all available submenus is displayed in this list. By selecting a submenu, you assign it to the created element.</p><h2>Assigned icon</h2>

<p>This is the area where you can change the default icon used for an item, taken e.g. from an action. Just select it from the list and assign it with the button marked with an arrow.</p><h2>Accepting an item</h2>
<p>After setting all the parameters of the new item, you can accept the item and add it to the bar. You can do this in two ways, or by using the "Add and Close" button, which will close the window and add an item to the bar; or with the "Add another" button, thanks to which the current item will be added to the toolbar, and the window will not close and you will be able to add more items.</p></td>
