<h1>Program Settings - Editor Font</h1>

<!-- tre&#347;&#263; pomocy -->
<p>The settings grouped in the "Editor font" category allow you to customize the document editor to your liking in terms of the font used. Here you can choose which font from among those available in the system will be the font for editing documents. The following settings are available in this group:</p><p><b>Use a fixed-width font (works faster)</b><br>
Check this option to use a fixed-width font in the editor. Enabling this option makes the editor run faster, but the font selection is narrower.</p><p><b>Use the specified font (works a bit slower)</b><br>
Select this option to use any font you choose in the editor. Enabling this option results in a slightly slower operation of the editor, but you can choose basically any font.</p><p><b>Typeface</b><br>
From this list, select the font that you want to use.</p><p><b>Character set</b><br>
Select the character set for the selected font from this list.</p><p><b>Font size</b><br>
In this field, indicate the size for the font used for editing documents.</p><p><b>Selection text color</b><br>
From this list, select the color you wish to use as the text color for the selection text.</p><p><b>Selection background color</b><br>
From this list, select the color you wish to use as the background color of the selected text.</p><p><b>Font settings preview </b> <br>
Here you can see how the selected font will look in the editor.</p></td>
