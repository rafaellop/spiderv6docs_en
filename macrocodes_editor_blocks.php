<h1>Template Editor - Macro Blocks tab</h1>




<!-- tre&#347;&#263; pomocy -->

<p>Blocks contain pieces of macro code that may (or may not) be used when inserting macro code content into a document. This allows you to create complex template such as complete HTML documents where you can choose what to insert into the document and what not.</p>

<p>For example - when creating a macro code, you can enter IMG tag in blocks to embed graphics in an HTML document. The user using the template will be able to choose whether to insert the image or not (by enabling or disabling the block)</p>


<p>The Blocks used in macro code tab contains a list of blocks that have been created. They are operated using the menu on the right and the check box next to the block name.</p>

<p>In blocks - similar to the left, middle, and right sides of the template - you can use variables as well as insert other blocks.</p>

<p><b>Template blocks</b> <br>
Lists the available blocks. Check if the block is to be used by default when using template.</p>

<p><b>Adding a new block</b><br>
Click if you want to add a new block. Use the <a href="macrocodes_editor_addblock.php">Edit Template Block</a> window to create new blocks and edit existing blocks.</p>

<p><b>Rename selected block</b><br>
Click if you want to rename a block. If a block is used in the body of a macro code, its name will be automatically renamed in all block call symbols.</p>

<p><b> Editing the selected block </b> <br>
 Click if you want to edit the contents of the selected block. To create new and edit existing blocks, use the <a href="macrocodes_editor_addblock.php"> Edit Template Block </a> window.</p>

<p><b>Delete the selected block.</b><br>
Click if you want to remove the definition of the selected block from the template. Deleting a block definition will not remove its calls from the template content!</p>

<p><b> Remove all blocks </b> <br>
 Click if you want to remove all block definitions from the macro code. Removing block definitions will not remove their calls from the template content!</p>

<p><b>Move item up / Move item down</b><br>
Click to move the block up/down.</p>

