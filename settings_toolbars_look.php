<h1>Program settings - Appearance of toolbars and menus</h1>

<!-- tre&#347;&#263; pomocy -->
<p>In this category you will find settings that determine the appearance of toolbars and the program menu. You can define both the colors and the style of the menus and toolbars in the groups located here. Here are the different groups and their settings:</p><h2>The appearance and color of the toolbars in the style</h2> 

<p>In this group, select the option that corresponds to your preferred colors and appearance of the toolbars and menus.</p><h2>Use the colors from the set for the selected style</h2> 

<p>If you prefer one toolbars style but a different highlighting scheme, select the palette you want to use here.</p><h2>Settings</h2> 

<p><b>Shadow in the menu</b><br>
Check this option if you want an additional shadow to be drawn around the menu.</p><p><b>Gradient under the active menu item</b><br>
Check this option if you want the background of the menu item over which the mouse is positioned to be created using a color transition.</p><p><b>Inactive images in a lighter shade</b><br>
Check this option if you want the images of inactive button icons to be drawn in a slightly lighter shade.</p><p><b>Show arrows next to names in the main menu</b><br>
Check this option if you want arrows to appear next to the names in the main menu. The menu will be slightly easier to read, but will take up more screen space.</p><p><b>Icons in gray tones when inactive</b><br>
Check this option if you want the images of inactive button icons to be drawn in shades of gray.</p><p><b>Wrap buttons on the toolbar instead of displaying an arrow</b><br>
If a toolbar contains more buttons than can be displayed on the current panel where the toolbar is located, the buttons will, depending on this setting, be displayed in an additional submenu available under the arrow button or the entire toolbar will be "wrapped" so that all buttons are visible on the screen</p><h2>Menu shadow style</h2>

<p>If you chose a drop shadow for a menu in one of the options above, here you can define what style it will be displayed in.</p><h2>Gradient below the active menu item</h2>

<p>If you have chosen a gradient for active menu items in one of the options above, you can specify here how the color transition will be drawn: from left to right or from top to bottom.</p><h2>Gradient on the menu</h2>

<p>If you want the background of the entire menu to be drawn using a color blend, select the style of this transition you choose here.</p><h2>Gradient under the pictures in the menu</h2>

<p>If you want a color transition on the background to also be placed under the pictures in the menu, select the selected item here that determines the style of this transition.</p></td>
