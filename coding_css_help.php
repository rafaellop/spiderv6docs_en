<h1>Support for CSS - Context-sensitive help</h1>

<!-- tre&#347;&#263; pomocy -->



<p>It can be very helpful to use contextual help for CSS. Contextual Help is based on the generally available <a href="https://www.w3.org/Style/CSS/" target="_blank"> CSS recommendation on the W3C Consortium website </a> .</p>

<p>Thanks to the documentation support mechanism built into the program, you can use it at any time and go straight to the topic of the selected property! To read information about the selected property, place the cursor on it and use the keyboard shortcut <tt>Ctrl+F1</tt>. You can also use the command in the <a href="main_menu_editor.php">editor's popup menu</a>. The help text will be displayed on the <a href="down_help.php">bottom panel</a>. This mechanism works both in the <a href="main_editor.php">editor</a> and in the <a href="main_tabs_inspector_css.php">CSS Inspector</a>.</p>

<p>For example, you can put your cursor on the <tt>font-family</tt> property, press Cltr+F1. The bottom panel will appear, and it will immediately display the page for that property.</p>

<p><b>Attention!</b><br>
All CSS help text is in English.</p>

