<h1>Program settings - On startup</h1>

<!-- tre&#347;&#263; pomocy -->
<p>The category named "On startup" allows you to define the program's startup behavior. This allows you to select a folder to switch to, enable opening of recently edited documents, etc. This group includes the following settings:</p><p><b>Display the "About" window on start-up</b><br>
Check this option to enable the display of the 'About' window at the start of the program.</p><p><b>Display the welcome window and program mode selection</b><br>
Check this option to enable the display of the program's welcome window, together with the possibility to select the mode of the main program window.</p><p><b>Display the "Tios" window when starting the program</b><br>
Check this option to enable the display of a hints and tricks window at program start-up.</p><p><b>Show the tray icon next to the clock and minimize to it</b><br>
Check this option to enable the program window to minimize to the icon in the system tray next to the clock, instead of the taskbar. Disabling it will minimize the program normally and disable the tray icon. When the tray icon is right-clicked, a menu appears with several useful commands (restoring the program window, disabling the program, links to pages about Spider on the Internet, etc.).</p><p><b>Last used folder as current at program start-up</b><br>
Check this option to cause the active folder to always be set to the same folder that was used the last time the program was started.</p><p><b>Always the same folder as the current one when starting the program</b><br>
Check this option to cause the active folder to be set to the folder selected below.</p><p><b>Start-up folder</b><br>
In this field, enter or select using the button next to it the folder that will be set as the active folder when the program is started.</p><p><b>Remember edited documents for the program</b><br>
Check this option to have the program open all documents that were open when the program was previously closed.</p><p><b>Remember edited documents for each project separately</b> <br>
Select this option to open all documents that were open when you edited each project individually.</p><p><b>Do not remember edited documents</b><br>
Select this option to prevent the Spider from remembering the list of recently opened documents.</p></td>
