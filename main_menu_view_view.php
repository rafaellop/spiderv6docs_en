<h1>Main menu - View - View</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>With this menu you can change the appearance of the main program window, and thus switch to the view of panels, toolbars, etc:</p>

<p><b>Toolbars</b><br>
A submenu containing toggles to select <a href="toolbars_general.php">the toolbars</a> visible in the main program window.</p><p><b>Resource panel on the left</b><br>
A submenu containing switches to activate one of the tabs of the <a href="main_tabs_general.php#tabs_leftpanel">panel at the left</a> in the main program window. The default keyboard shortcut is <tt>F9</tt></p><p><b>Resources panel on the right</b><br>
A submenu containing switches to activate one of the tabs of the <a href="main_tabs_general.php#tabs_rightpanel">panel on the right</a> in the main program window. The default keyboard shortcut is <tt>Ctrl+F9</tt></p><p><b>Results panel</b><br>
A submenu containing switches to activate one of the <a href="down_panel.php">panel</a> tabs <a href="down_panel.php">at the bottom of</a> the main program window. The default keyboard shortcut is <tt>Left Alt+F9</tt></p><p><b>Status bar</b><br>
Click to enable or disable the status bar view at the bottom of the main program window.</p><p><b>Full screen</b><br>
This command enables or disables full-screen mode, during which only the editing or preview area, the mini-bar and the menu are visible. The default keyboard shortcut is <tt>Shift+F9</tt>.</p><hr>
<p>The next three commands work similarly to the corresponding buttons in the upper right corner of the program window. However, thanks to the fact that they are located in the program menu, you can now easily assign hotkeys to them.</p>

<p><b>Maximize</b><br>
Click to maximize the program window.</p><p><b>Minimize</b><br>
Click to minimize the program window.</p><p><b>Restore</b><br>
Click to restore the minimized program window to the screen.</p><hr>

<p><b>Main window operating mode</b><br>
Click to launch a tool that allows you to easily select your preferred working mode. A description of the tool can be found in <a href="tool_welcometo.php">a separate chapter</a>.</p></td>
