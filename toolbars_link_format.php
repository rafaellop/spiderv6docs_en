<h1>Toolbars - Links, text, formatting</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>The toolbar contains commands that launch tools for inserting and editing links, as well as for formatting and handling text.</p>


<p><b><a href="tool_link.php">Inserting a link</a></b><br>
Click to launch the tool for inserting a <tt>A</tt> link (default shortcut <tt>Shift+Ctrl+A</tt>).</p><p><b><a href="tool_link_map_editor.php">Links map editor</a></b><br>
Click to launch the link map editor.</p><p><b><a href="down_links_verify.php">Verification of links in the document</a></b><br>
Click to verify the validity of the links in the document.</p><p><b><a href="tool_comment.php">Inserting a comment</a></b><br>
Click to launch the comment insertion tool. The tool automatically recognises the current programming language and inserts the appropriate comment characters. Default shortcut <tt>Shift+Ctrl+K</tt>.</p><p><b><a href="tool_marquee.php">Animated text</a></b><br>
Click to launch the tool to facilitate the insertion of animated text <tt>MARQUEE.</tt></p><p><b><a href="tool_text_attributes.php">Text formatting</a></b><br>
Click to launch the tool for inserting text formatting tags.</p><p><b> <a href="tool_text_attributes.php">New paragraph tag</a></b><br>
Click to insert a new paragraph tag (depending on the <a href="settings_codeinsight_additional.php">Program Settings</a> - <tt>p</tt> or <tt>div</tt>). If you click on the arrow to the right of the command, formatting commands will expand (identical to the <a href="main_menu_htmlcode_align.php">HTML Code / Alignment menu ) </a>.</p><p><b>Unbreakable space tag</b><br>
Click to insert a code to indicate an unbreakable space - <tt>&amp;nbsp;</tt>. Default keyboard shortcut <tt>Alt+Space</tt>.</p><p><b> Bold text tag</b><br>
Click to insert a bold text tag. If you click on the arrow to the right of the command, a <a href="main_menu_htmlcode_format.php">submenu</a> will expand that allows you to insert other text formatting tags (identical to the <a href="main_menu_htmlcode_format.php">HTML Code / Text Formatting menu ) </a>.</p><p><b>Headline</b><br>
Click to insert a 1st degree header ( <tt>H1</tt> tag). You can also expand the menu for inserting other degree header tags (identical to the <a href="main_menu_htmlcode_headers.php">HTML Code / Header</a> menu).</p><p><b>Breaking the line BR.</b><br>
Click to insert a line breaking tag. You can also expand the menu containing items identical to those in the <a href="main_menu_htmlcode_brnewline.php"> HTML Code / Line Breaking BR</a> menu <a href="main_menu_htmlcode_brnewline.php">.</a></p><p><b>Replace selected text with...</b> <br>
Click to open a list with possible ways to replace the selected text. This list contains items identical to those in the menu <a href="main_menu_edit_change_selected.php">Edit / Selected text to...</a>.</p><p><a href="tool_lists.php">Enumerations </a> <br>
Click to run <a href="tool_lists.php"> Enumeration Generator </a> . You can also expand the menu for inserting enumeration tags (identical to the <a href="main_menu_htmlcode_lists.php"> HTML Code / Enumerations </a> menu).</p></td>
