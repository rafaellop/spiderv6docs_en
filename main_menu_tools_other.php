<p>The menu contains commands that launch other tools useful when working with the program.</p>

<p><b><a href="tool_zoomek.php">Magnifying glass</a></b><br>
Click to activate the magnifying glass. The tool shows a magnified image of the screen in the vicinity of the mouse cursor.</p>

<p><b><a href="tool_recorder.php">Activity Recorder</a></b><br>
Click to start the Activity Recorder. The tool allows you to play back the recorded operations later.</p>

<p><b><a href="tool_colorpalette.php">Color Palette</a></b><br>
Click to launch the color palette (default shortcut <tt> Shift + Ctrl + C </tt> ). The tool allows you to select a color from the palette, as well as take a color sample from anywhere on the screen with the help of a pipette.</p>

<p><b><a href="tool_unicodeeditorek.php">Unicode editor</a></b><br>
Click to launch the mini editor window, which is used to paste and handle Unicode-encoded texts.</p>

<hr>

<p><b>Renaming multiple files</b><br>
This command launches the <a href="tool_chameleon.php">Chameleon file ren</a> aming tool and allows files in multiple folders to be renamed at once using set rules.</p>



