<h1>Main menu</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>Spider V6 has ten items in the main menu. The layout of commands in individual menus has been specially designed with ergonomics in mind.</p>

<p>Some items are repeated in different menus, making it easier to find tools depending on the context in which you are looking for them. For example, some tag editing tools are located in both the HTML Code menu and the Tools menu. Additionally, you can find the same commands in <a href="toolbars_general.php">toolbars</a> .</p>

<h2>List of items in the main menu</h2>

<ul>
  <li><b><a href="main_menu_file.php">File</a></b> - the menu is used to perform basic operations on documents and the program, such as opening, closing, saving or printing. <br><br></li>
  <li><b><a href="main_menu_services.php">Projects</a></b> - The commands in this menu are used to work with files and folders on the local drive. You can also use this menu to create <a href="services_general.php">projects</a> and switch between them.<br><br></li>
  <li><b><a href="main_menu_edit.php">Edit</a></b> - This menu contains useful commands for working with the document.<br><br></li>
  <li><b><a href="main_menu_find.php">Search</a></b> - the menu is used to launch a tool for searching and replacing text in documents opened in the program, as well as those saved on disk.<br><br></li>
  <li><b><a href="main_menu_htmlcode.php">HTML code</a></b> - In this menu, you will find commands for inserting any HTML tags.<br><br></li>
  <li><b><a href="main_menu_htmlhelp.php">HTML Help</a></b> - the menu contains a number of commands for quick <a href="tool_htmlhelp_general.php">HTML Help</a> operation. <br><br></li>
  <li><b><a href="main_menu_spelling.php">Spelling</a></b> - the commands in this menu are used to check for correct spelling, character encoding, etc.<br><br></li>
  <li><b><a href="main_menu_tools.php">Tools</a> </b> - the menu contains a number of submenus that contain various tools available in Spider. <br><br></li>
  <li><b><a href="main_menu_view.php">View</a></b> - using this menu you can display various elements of the program, such as individual panels, settings windows, etc. <br><br></li>
  <li><b><a href="main_menu_help.php">Help</a></b> - Use this menu to access commands that help you get help using the program and Internet technologies. <br><br></li>
</ul>

