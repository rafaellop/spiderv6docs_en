<h1>Using Help</h1>

<p>The help for Spider is designed to make it as easy as possible to find terms related to that particular topic or tool. Most dialog boxes, mainly tool boxes, have a "Help" button in their window, which instantly brings up the appropriate help topic on the screen.</p>

<p>Help can also be recalled easily by pressing the F1 key. In dialog boxes, help is available just as easily. To find out the meaning of the selected item, click the button on the top bar of the window, and then click the desired item.</p>

<p>Context-sensitive help is very useful when creating code in HTML, PHP, and CSS. Just place the cursor on an HTML tag, the name of a PHP built-in function or CSS property, and press Ctrl + F1 to get help for that element.</p>

<p>In addition, the <a href="down_help.php">program Help</a> tab on the bottom panel provides specifications, courses and instructions for HTML, XHTML, CSS, PHP, JavaScript.</p>


