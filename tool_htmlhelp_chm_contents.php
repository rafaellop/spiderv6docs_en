<h1>HTML Help - HTML Help Content</h1>



<p>Using this tool, we can view the contents of already compiled CHM files attached to the project, select specific items from them and attach them to the project as a reference.</p><h2>Launching</h2>

<p>You can launch the described tool with the button marked with the icon shown above. The button for launching this window can be found, for example, in the <a href="tool_htmlhelp_hhc_editor.php">editor of the table of contents</a> or <a href="tool_htmlhelp_hhk_editor.php">index</a>.</p><h2>Window contents</h2>

<p><b>CHM file</b><br>
In this field we enter the name of the CHM file along with the path, or using the button next to the field we open a window with a list of directories and files, from where we select the desired file.</p>
<p><b>Filter</b><br>
In this field you either type or select from a list the file extensions you want to show in the window below. This allows, for a large project with many files, to limit the number of files to only the desired ones. Next to the checkbox is a content refresh button in case we change the filtering conditions.</p>
<p><b>File list window</b><br>
This window will display a tree containing the files contained in the selected HTML Help file. If you set a restriction in the filtering field, only files according to this condition will be displayed and their names with extensions will be given. To complete the selection process, select one of the files in this list and click on the "OK" button.</p>



