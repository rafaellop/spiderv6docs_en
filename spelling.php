<h1>Spell checking</h1>





<p>Spider has built-in tools for checking the spelling of documents.</p>

<p>Using the <a href="main_menu_main.php">Main menu</a> / <a href="main_menu_spelling.php">Spelling</a> commands, you can run the spell checker both for the current document and - after activating the <a href="find_ext.php">Extended Search and Replace</a> tool - for documents of the entire project and selected folders.</p>

<p>When working in the editor, in <a href="main_editor.php">the source code editing mode</a>, you can enable underlining of spelling errors (with the command in the <a href="toolbars_minitoolbar.php">mini editor bar</a>).</p>

<p>Dictionaries for a number of languages, including Polish and English of course, have been supplied with Spider.</p>

<h2>Thesaurus</h2>

<p>In addition to the spell-checker, you can also use the Thesaurus (a dictionary of close words). For the thesaurus, only the English dictionary is available.</p>

<p>To use the thesaurus, select the dictionary and then activate the thesaurus with the command from the <a href="main_menu_spelling.php">Spelling menu</a>.</p>

<h2>Autocorrect</h2>Auto-correction allows incorrect words to be corrected automatically as soon as they are typed in. The corresponding options can be found in <a href="settings_editor_spelling.php">Program settings / Other settings / Editor ... / Spell-check</a>.<h2>Other tools</h2>

<p>In addition to the typical spell check, you can also use the <a href="tool_typo_correct.php">Typographic Correction</a> function, which will quickly adjust the text so that it fits nicely on the page.</p>

<p>In the <a href="main_menu_spelling.php">Spelling menu</a>, there are also tools to quickly convert the <a href="charset_encoding.php">encoding of national characters</a> or remove Polish letters from documents.</p>



