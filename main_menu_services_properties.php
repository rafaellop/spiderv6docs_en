<h1>Main menu - Projects / Project properties</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>The commands in this menu are used for the current project of the site, such as searching and replacing text in site files, synchronizing the site with the server content, etc. The following commands are available:</p>

<p><b><a href="service_properites.php">Project properties</a></b><br>
Click to open the project properties window.</p><p><b><a href="service_todotasks.php">To-Do List</a></b><br>
Click to open the To-Do window. It will contain all the tasks of all project documents.</p><hr>

<p><b><a href="down_links_verify.php">Link validation</a></b> <br>
 Click to verify links in the project documents.</p><p><b><a href="down_links_verify.php">Syntax verification</a></b><br>
Displays a list of commands to perform syntax verification of project documents.</p><p><b><a href="down_integrity.php#service_clean_quick">Project Cleanup</a></b> <br>
Click to clean the site. The command has a submenu where you will find additional commands, such as <a href="down_integrity.php">Checking website consistency</a> , or searching for its unnecessary elements (eg "orphan" * .ADD files).</p><hr>

<p><b><a href="service_tool_synchronization.php">Project synchronization</a></b> <br>
 Click to run the site-to-content synchronization tool.</p><p><b><a href="service_archive.php">Project archiving</a></b><br>
 Click to launch the project archive tool.</p><hr>

<p><b><a href="find_ext.php">Search and Replace</a></b> <br>
Click to run Extended Search and Replace in project documents.</p><p><b><a href="charset_encoding.php">Character encoding conversion</a></b> <br>
Click to change the character encoding in project documents.</p><p><b><a href="spelling.php">Project spellchecking</a></b> <br>
Click to check spelling in project documents.</p><p><b><a href="tool_typo_correct.php">Project typographic correction</a></b> <br>
Click to launch the typographic correction tool that allows you to format the text in your project documents in such a way that it looks better on the page.</p><p><b><a href="formatter_general.php">Format the source code of project documents</a></b> <br>
Click to run source code formatting in project documents.</p><p><b><a href="service_map.php">Sitemap</a></b> <br>
Click to open the site map graphically showing all the links between project documents.</p><p><b><a href="service_properites.php#servicepropertiesoftenused">Open frequently used project documents</a> </b> <br>
 Click to open all frequently used project files.</p><p><b><a href="service_properites.php#servicepropertiesoftenused"> Add open document to project frequently used list</a></b> <br>
 Click to add the current document to the frequently used files for the project.</p><hr>

<p>The last two menu items are only available if the option is selected in the properties of the current project that <a href="service_properites.php#htmlhelp">it is also an HTML Help project</a>.</p>

<p><b>HTML Help</b><br>
