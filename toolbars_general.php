<h1>Toolbars - in general</h1>




<!-- Tre&#347;&#263; pomocy -->

<p>The layout of the bars and their contents have been specially designed for ergonomic work. All commands, tags, etc. are intuitively available just where you would expect them.</p>

<p>Individual bars can be toggled on, off, moved within the program, and even docked, making the bar a so-called floating window.</p>

<p>It is also possible to add, remove and change the order of commands on the toolbars. This allows you to customize the program's interface according to your needs.</p>


<h2>List of toolbars</h2>

<p>Spider has eight toolbars with buttons and drop-down menus for inserting page elements, controlling program operation, etc:</p>

<ul>
        <li><a href="toolbars_main.php">Main toolbar</a> - contains commands for creating a new document, undo and redo operations, printing, etc.<br><br></li>
        <li><a href="toolbars_often.php">Frequently used</a> - contains frequently used commands, such as inserting images, links, code formatting and syntax verification tools, etc.<br><br></li>
        <li><a href="toolbars_link_format.php">Links, text and formatting</a> - contains commands that insert any HTML tags into the document.<br><br></li>
        <li><a href="toolbars_tables.php">Tables and forms</a> - contains commands that launch tools for creating and editing tables and forms<br><br></li>
        <li><a href="toolbars_gfx.php">Image and multimedia</a> - contains commands that launch tools for inserting graphics, multimedia into a document, creating galleries and converting graphics, etc.<br><br></li>
        <li><a href="toolbars_objects_scripts.php">Java, ActiveX, SSi and others</a> - contains commands that run tools for embedding various objects in a document, such as Java applets, ActiveX controls, scripts, etc.<br><br></li>
        <li><a href="toolbars_minitoolbar.php">Mini editor toolbar</a> - contains commands for day-to-day operation of the document, such as changing syntax coloring, enabling and disabling visual editing, launching the document information window and others.<br><br></li>
        <li><a href="toolbars_htmlhelp.php">HTML Help Tools</a> - contains a tool for HTML Help.<br><br></li>
</ul>

<p>You can also use three additional bars, where you can place any commands available in Spider.</p>

<h2>Customize the toolbars to suit your needs</h2>

<p><b>Moving the toolbar</b>
To move the toolbar to another location, grab it from the left side (if the toolbar is arranged vertically, then at the top) and drag it to the target location. If possible, the bar will dock at that location. Otherwise, it will create a floating window.</p>

<p><b>Customizing existing toolbars</b>
To customize the bars, right-click on any of the bars and select <a href="settings_toolbars_toolbars.php">Customize Bars, Menus and Shortcuts</a>.</p>

<p><b>Wrapping toolbars that are too long</b><br>
If a toolbar is too long to fit in the space provided for it, it can be wrapped. For bars displayed vertically, the icons that did not fit will be displayed in the next column. For horizontal bars - in the next row. <br>
Alternatively, instead of wrapping excessively long bars, a button can appear at the end of each such bar, which, if clicked, will display the remaining contents of that bar. The relevant options can be found in the <a href="settings_toolbars_look.php">Program Settings - Appearance of Bars and Menus</a> window, which can be accessed most quickly using the <b>Customize Bars,Menus</b> command and the <b>shortcut</b> in the context menu for any toolbar and menu.</p></td>
