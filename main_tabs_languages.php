<h1>Languages tab</h1>



<p>The "Languages" tab contains a set of definitions for several programming languages supported by Spider. You can turn it on using the panel on the right, as well as from the menu under the button shown above or from the main menu. The languages available here are:</p><p>
</p><table width="100%" border="0">
<tr>
<td></td>
<td>Tree of markup languages defined in TDF files</td>
</tr>
<tr>
<td></td>
<td>PHP language tree</td>
</tr>
<tr>
<td></td>
<td width="100%">JavaScript language tree</td>
</tr>
<tr>
<td></td>
<td>VisualBasic Script language tree</td>
</tr>
<tr>
<td></td>
<td>ASP language tree</td>
</tr>
</table>

<p>Definitions of programming languages are presented in the form of a tree with appropriate categories related either to the type of object whose methods and properties they contain, or to the categories (e.g. in PHP) created in the definition file editor.</p><p>The described tab can be, for example, a cheat sheet. It is not easy to remember functions or properties for all those programming languages that are increasingly entering network technologies. For items selected in the tree, a hint area is displayed in the lower part of the tab, providing more information about the selected item. For example, for <tt> window / location </tt> we will get a hint:</p><p><tt>
<b>Name:</b> location<br><b>Description:</b> URL of the content to be displayed in the window<br><b>Type:</b> location<br><b>Support:</b> JavaScript &gt;= 1.0<br></tt></p><p>As you can see, the information presented in this area is quite useful.</p><p>You can use this tab not only as a cheat sheet. You can also insert selected items into your document. You can do it in two ways: by using the button in the left part of the "Programming languages" tab or by double clicking on the selected element. The Spider will warn you if you want to put the element in the wrong place.</p><p>If you select the markup languages view, you will be able to select from the drop-down menu the language whose tags, attributes and events you would like to see in the tree.</p></td>
