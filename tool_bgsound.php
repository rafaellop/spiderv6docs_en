<h1>Tools - Insert sound or music</h1>

<!-- tre&#347;&#263; pomocy -->

<p>The window is used to insert links to sound files playing in the background ( <tt>BGSOUND</tt> tag in HTML). This tag is only supported by Internet Explorer, so the sound may not be heard by users of other browsers.</p><p>The tool can be launched from the <a href="main_menu_main.php">Main Menu</a> / <a href="main_menu_tools.php">Tools</a> / <a href="main_menu_tools_gfx.php">Graphics and Multimedia</a> menu. The command icon is shown above.</p>

<p><b>Sound or music</b><br>
Enter the path to the sound file. You can use the window opened with the button next to the field. Supported formats include <tt>WAV</tt>, <tt>MIDI</tt> and <tt>AU</tt>.</p>

<p><b>Repeat</b><br>
Specify how many times the indicated sound is to be played. If you select the<b>"infinite</b>" option, the sound will be played an infinite number of times.</p>

<p><b>Include EMBED for Netscape</b><br>
Check if you also want to insert the EMBED tag, which is supported by Netscape Navigator, Mozilla, etc. browsers.</p>



