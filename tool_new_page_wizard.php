<h1>Tools - New page from template wizard</h1>


<p>This wizard allows you to quickly create a new, complete document based on the page templates also available on the <a href="main_tabs_macrocodes.php">Templates tab</a>. The tool turns on automatically when you first start the program.</p><p>The wizard consists of three boards. To switch between them, use the Next and Back buttons located at the bottom of the window.</p>

<h2>Running the wizard</h2>

<p>At the top of the window you will find two options responsible for running the wizard:</p><p><b>Display this window when launching Spider</b><br>
Leave checked if you want the wizard to run automatically every time you start the program.</p><p><b>Display this window when a new document is created</b><br>
Leave this option checked so that the default action when creating a new document is to run this wizard. If you uncheck this option, a new blank document will be opened in the editor.</p><p>At any time while the program is running, you can run the wizard using the commands available in the File / New Document menu, or Tools / Tag Code Wizards</p>

<h2>Choosing a page template</h2>

<p>Below the switches responsible for running the wizard is the most important element of the tool - a page that allows you to select a template. You can choose from dozens of page templates collected in two categories - Commercial and Private. This division is, of course, only conventional. Simply put, in the Commercial category there are templates for pages that look more "official", while in the Private category there are templates that bass more for creating your own home page, etc.</p>
<p>After selecting a template from the tree, a preview image will appear on its left side.</p>
<p>If you decide to use a particular template, click on the Next button at the bottom of the page.</p>


<h2>Document settings and content</h2>

<p>The elements collected on the second page allow you to customize the page. A list of blocks is placed on the left side. Each block denotes an element that can be enabled or disabled during page generation. In the case of page templates, these are the page's menu elements (i.e. buttons to the other subpages).</p>
<p>Two tabs have been placed on the right side of the page:</p>

<p><b>Content of the new page</b><br></p><p>In this tab you can enter the content of the document. A sample text is placed here by default. It is not necessary to change it, because after generating the HTML document and opening to in the editor, you will be able to change it at any time.</p>

<p><b>Advanced</b><br>
This tab contains the inspector of variables of the template used to generate the page. These are variables containing the page title, tooltip balloons, paths to files attached to the template (e.g. graphics, etc.). Most often, nothing needs to be changed here.
You can read more about this in the help topic <a href="how_pagefrom_macrocode.php">How to create an attractive page quickly</a>.</p><h2>Preview of the generated page</h2>

<p>The third wizard page is used to view the finished page in the built-in browser. In addition, it is possible to view the document in a new window of the default browser on the system, as well as to select the browser component used for previewing in Spider (MS Internet Explorer, or Mozilla).</p>


<h2>Save to disk and edit the document</h2>

<p>By the time you run the preview, the document has already been saved to disk. You can close the wizard with the <b>Close</b> button at the bottom of the window. However, you will probably want to edit it further and customize it, fill it with content, etc. To do so, you can click the <b>Open for editing</b> button. The wizard will finish its work and the newly generated document will be opened in the editor.</p>


