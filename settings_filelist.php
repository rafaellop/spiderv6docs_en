<h1>Program settings - File list</h1>

<!-- tre&#347;&#263; pomocy -->
<p>The category called "File list and opening" allows you to define the behavior of the file list in the "Projects" panel, as well as the operation of the "Move &amp; Drop" function. You can also assign extensions to specific file categories here. This group includes the following subgroups: "File List &amp; Opening", "Drag &amp; Drop", "File Filters".</p><h2>File list and opening</h2>

<p>This sub-group of settings relates directly to the behavior of the file list. The following options are available here:</p><p><b>Display folders in the file list</b><br>
Check this option to cause folders to be displayed in the file list. If you disable this option, only files will be displayed in the file list.</p><p><b>Change folder to the folder of the document being opened</b><br>
Check this option if you want the current folder to be changed to the folder where the document being opened is located when the file is opened.</p><p><b>Change the folder to the folder of the saved document</b><br>
Check this option if you want the current folder to be changed to the folder where the document is being saved when the file is saved.</p><p><b>Change the folder to the folder of the activated document</b><br>
Check this option if you want the current folder to be changed to the folder in which the document you are switching to is saved when switching between documents.</p><p><b>Insert local links in lowercase letters</b><br>
Select this option to always insert links to files that you insert into documents in lowercase letters.</p><p><b>Hide BAK backup files in the file list</b><br>
Check this option if you want to hide the backup copies (BAK) made while working on documents in the file list.</p><p><b>Move deleted files to the Recycle Bin</b><br>
Check this option to ensure that files you delete go to the Windows Recycle Bin so that they can possibly be recovered.</p><p><b>When deleting files from a drive, also delete their backups</b><br>
Check this option if you want to delete the BAK backup files together with the original files of which the BAK files are copies.</p><p><b>Hide the file list and directory tree button bar</b><br>
Check this option if you do not want a button bar to be placed next to the file list and directory tree on the 'Projects' tab.</p>

<p><b>Display overlay icons in the list of files and folders</b><br>
Disable this option so that the program does not display 'overlays' on file and folder icons. This will speed up refreshing the file list.<br>
Overlay icons are used by various programs, such as CVS version control systems, etc.</p><h2>"Drag and Drop" support</h2>

<p>This subgroup of settings applies to the operation of moving and dropping files within the file list. The following options are available here:</p><p><b>When moving HTML files from the file list, insert a link </b> <br>
Select this option so that moving files with the mouse from the file list above the editor will not open them for editing, but inserting a link to them.</p><p><b>Moving other files from the file list edit in the tool</b><br>
Check this option tool ensure that moving files other than HTML documents from the file list above the editor with the mouse causes them to open for editing in the corresponding tool.</p><p><b>When moving HTML/PHP files into a PHP block, include the following in the document</b><br>
Check this option to ensure that moving HTML and PHP document files from the file list over the editor with the mouse does not open them for editing, but inserts a PHP-style inclusion construct, e.g. include ("file_name").</p><h2>File filters</h2>

<p>This subgroup of settings allows you to assign selected extensions to specific groups of files and thus allows you to filter files appropriately in the file list. The following settings are available:</p><p><b>File filter - HTML documents</b><br>
In this field, enter semicolon-separated extension names for the file types you wish to use as HTML documents. The extensions should be of the form e.g.. *.html;*.php.</p><p><b>File filter - Image files</b><br>
In this field, enter semicolon-separated extension names for the file types you wish to use as image files. The extensions should be of the form e.g.. *.gif;*.jpg</p><p><b>File filter - Web files</b><br>
In this field, enter semicolon-separated extension names for those file types you wish to use as web files, i.e. files used on pages (e.g. animations). The extensions should be of the form e.g. *.avi;*.swf.</p><p><b>File filter - Hidden files</b><br>
In this field, enter semicolon-separated extension names for those file types that you want to always be hidden in the file list on the<a href="main_tabs_myservices.php">"Projects</a>" tab.</p><p><b>Warn when opening files without extension</b><br>
Check this option if you want the program to display a warning when trying to open a file without an extension.</p>

<h2>File Associations with Spider</h2>

<p>In this group of settings you can associate the Spider with selected file types. Here you will find a list with the types, and buttons for binding and deleting it.</p><p>On the list you will find the names of file types and file extensions for which you can assign a relationship with a Spider in the context menu of Windows explorer. Additionally, the linkage is established for the files of the selected type of the Spider-style icon.</p><p>To associate selected file types with Spider, select them using the switch and press the assign button.</p>
<p>To unbind for selected types, uncheck these types on the list and click the assign button.</p>

<p><b>Please note that appropriate permissions are required to change the links in the system!</b></p></td>
