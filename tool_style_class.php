<h1>Tools - Select or create a class</h1>

<p>The tool described here allows you to create new classes used in CSS cascading styles. Classes allow you to assign styles defined in the stylesheet to tags separately from the styles for the selected tag. This allows you to assign a different style to the selected tag than it has declared in the stylesheet. For more on creating styles, see the topic on <a href="coding_css.php">creating styles in Spider</a>.</p><p>The class creation tool is launched with the button shown above in the tag insertion windows, which have this button and the ability to set the style.</p><h2>Window appearance</h2>

<p>The window consists of a list of classes, which presents all classes available in the currently edited document. In the list you will find two columns with the name of the class and the name of the document where it is declared. Next to the list are two buttons that allow you to create a new class using <a href="tool_style_editor.php">the style editor</a> and a button that allows you to create a new empty class. In both cases you will be asked to enter the name of the class.</p></td>
