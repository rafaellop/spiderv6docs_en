<p>Galleries containing photos, images and other graphics are common on websites. Spider makes it very quick and easy to create such a gallery. This can even be done by people who are completely unfamiliar with website creation.</p>

<h2>How do I create an image gallery?</h2>

<p>To create a gallery, run <a href="tool_gallery.php"> Image Gallery Creator</a>.</p>

<p>The first page is a greeting and introduction to the tool. The gallery creator will guide you through the process of creating it. In the following pages you can:</p><ul>
   <li>select images for the gallery (you can automatically add all files from a selected folder or from several folders)</li>
   <li>set gallery appearance options (number and layout of rows and columns, images and location of buttons, gallery graphic design)</li>
   <li>indicate the location of the target gallery files (html documents and images and their automatically generated thumbnails in the selected format)</li>
   <li>set options for creating thumbnails (scaling method, dimensions, compression ratio)</li>
</ul>

<p>On the last page, you will be able to view the finished gallery before saving it to the target location on the disk - if necessary, you can go back to the previous boards and correct the appropriate options.</p>

<p>For detailed information on the wizard, see the <a href="tool_gallery.php">Image Gallery Wizard</a> helpyou can launch by clicking on the Help button in the tool window.</p>





