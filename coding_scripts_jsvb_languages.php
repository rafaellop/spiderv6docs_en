<h1>Support for JavaScript and VBScript - Tab "Programming languages"</h1>



<!-- tre&#347;&#263; pomocy -->



<p>If you can't remember what a particular JavaScript or VBScript function is for, or you are looking for the right function for a particular task, the <a href="main_tabs_languages.php">Programming Languages</a> tab is very useful.</p>

<h2>Tool description</h2>

<p>This tab consists of a menu where you can switch programming languages and a data tree. If you select the view for JavaScript or VBScript, the tree contains branches containing a list of objects, and in the branches of each object a list of its supported methods.</p>

<p>The described tab can be used as a reference guide. It is not easy to remember the functions, constants and variables of JavaScript and VBScript. For elements selected in the tree, a tooltip area is displayed in the lower part of the tab, providing more information about the selected function. For example, for a <tt>Textarea</tt> object, we get a hint:</p><p><tt>
<b>Name:</b> Textarea<br><b>Description:</b> Represents a multi-line edit box<br></tt></p><p>You can use this tab not only as a reference tool. You can also insert selected elements into a document. You can do this in two ways: using the button on the left side of the tab or by double-clicking on the selected element. The Spider will warn you if you want to place an element in an inappropriate place.</p>



<h2>Editing a definition file</h2>

<p>The <a href="tagdata_editor_tdf_scripts.php">Definition File Editor for JavaScript and VBScript</a> is used to edit the data that the tool uses. Using it, you can change the description of an existing function, add new functions, etc.</p>


