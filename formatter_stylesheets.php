<h1>Formatting - Settings for CSS sheets</h1>





<!-- tre&#347;&#263; pomocy -->
<p>In this window, you can set options for an existing or new formatting style for CSS sheets. The window is divided into two parts:</p><ul>
	<li>Selectors and properties settings</li>
	<li>Visual style sheet layout</li>
</ul>

<p>The window can be called with the command <b> Add formatting profile </b> , or <b> Modify formatting profile </b> in the menu next to the <b> CSS formatting preset </b> list in the <a href="settings_codeinsight_formatter.php"> window. </a><a href="settings_codeinsight_formatter.php"> Program Settings - Source Code Formatting </a> .</p>


<h2>Selectors and properties settings</h2>

<p>In this section you can set options for individual selectors, element order, and case.</p>

<p><b>Order of selectors</b><br>
Select the sort order of the selectors:</p><ul>
	<li><b>alphabetically</b> - selectors will be arranged in<b>alphabetical</b> order</li>
	<li><b>no change</b> - selectors will not be sorted</li>
</ul>

<p><b> Properties order </b> <br>
 Select the sort order of the properties in the style:</p><ul>
	<li><b> alphabetically </b> - properties will be sorted alphabetically</li>
	<li><b>no change</b> - properties will not be sorted</li>
</ul>

<p><b>Names of selectors</b><br>
Choose what letters you want the selector names to be spelled with:</p><ul>
	<li><b>in uppercase</b></li>
	<li><b>lowercase</b></li>
	<li><b>no change</b></li>
</ul>

<p><b>Properties names</b> <br>
 Select the letters in which the property names are to be written:</p><ul>
	<li><b>in uppercase</b></li>
	<li><b>lowercase</b></li>
	<li><b>no change</b></li>
</ul>




<h2>Visual style sheet layout</h2>

<p>In this section you can set CSS layout options - line indentation, space between selectors, etc.</p>

<p><b>All properties in one line</b><br>
Select this option if you want all the properties of the class to be arranged on one line.</p>

<p><b>Each property in a new line</b> <br>
 Select this option if you want every property in the class to be placed on a new line. If you choose this option, you can also specify indent formatting:</p><ul>
	<li><b>Indentation size</b> - select how many characters use to indent a line</li>
	<li><b>Use tabs instead of spaces</b> - check this switch if you want to use tabs to create indentations</li>
</ul>

<p><b>Add blank lines between selectors</b><br>
Check this toggle if you want additional blank lines to be inserted between individual selectors. This increases the readability of the code somewhat.</p>

