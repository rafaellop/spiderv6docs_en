<h1>Coloring Scheme Editor - Keywords</h1>

<!-- tre&#347;&#263; pomocy -->

<p>Keywords are strings of characters that are permanently embedded in a particular coloring scheme. These are usually the names of functions and instructions in programming languages, the names of tags and their attributes in HTML, etc.</p>
<p>The different types of keywords are grouped into sets. It is possible to create an unlimited number of keyword sets, each of which can be assigned a different coloring style.</p>
<p><b>Caution: </b>do not overdo the number of collections/sets and the number of words in the collections when using older hardware. This puts a heavy load on the program and can noticeably slow down the editor, especially when switching between documents. For this reason, Spider has a number of different syntax coloring schemes and allows you to switch between them as you work, in order to put as little strain on the system as possible.</p>

<p><b>Enable keyword coloring</b><br>
If you select this option, it will be possible to define sets of keywords. A coloring style can be created and assigned for each set of keywords.</p>

<p><b>Collections</b><br>
The list contains collections of keywords defined in the schema. Collections can be added, deleted and edited by clicking on the icon to the right of the collection list.</p>
<p><b>NOTE:</b> Keyword sets can be arranged in any order by dragging. If the same keyword occurs in two sets, it will be colored according to the style for the set that occurs earlier (higher in the list).</p>

<ul>
<li><b>Edit keyword sets</b><br>
Click to change the name of a collection, as well as to assign to a collection or create a new coloring style.</li>
</ul>

<p><b>Current set of keywords</b><br>
Contains all the words assigned to the currently selected collection. You can add, subtract and edit the keywords of the selected collection by clicking on the icon to the right of the keyword list. The words in the list are automatically sorted in alphabetical order.</p>

<ul>
<li><p><b>Load keywords</b><br>
Click to load a set of keywords from a text file. Each keyword in the file must be placed on a separate line.</p></li>

<li><p><b>Save the keywords</b><br>
Click to save a set of keywords to a text file.</p></li>
</ul>

