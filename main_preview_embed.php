<h1>"Browse" tab</h1>

<!-- tre&#347;&#263; pomocy -->




<p>The "Browse" tab is located in the <a href="main_tabs_general.php#tabs_center">middle pane</a> , which you cannot disable. On this tab you can view the currently created document. It also allows you to preview templates if this option has been set on the <a href="main_tabs_macrocodes.php">Templates tab</a> .</p>

<p>Due to the rapidly changing programming offer of large companies and newer extensions introduced by them to the HTML language, it was decided that Spider would not be equipped with its own preview system of the created page. However, strong emphasis was placed on facilitating work with browsers available on the market.</p>


<p>From the Spider's level, you can immediately preview the edit field (default <tt> F11 </tt> key) of the currently edited page using the "Browse" tab. This function requires one of the browsers installed on the system</p><ul>
  <li>MS Internet Explorer version 3.02 or later</li>
  <li>Mozilla 1.x</li>
</ul>

<p><b>Note: Preview in Mozilla 1.6</b><br>
At the moment, in order to get a working built-in preview in Mozilla 1.6 you may need to install the corresponding control available at: <a href="https://www.adamlock.com/mozilla/MozillaControl1712.exe" target="_blank">https://www.adamlock.com/mozilla/MozillaControl1712.exe.</a></p><h2>"Browse" tab menu</h2>The tab has a menu that allows you to adjust the viewing options for currently edited documents:<p><b>Address field</b><br>
The address of the document you are currently viewing appears in this field. You can also enter the address of another document here and press <tt>Enter</tt> to display it.</p><p><b>Go to the previously viewed</b><br>
Click to return to the previous page.</p><p><b>Go to the next, already viewed</b> <br>
Click to go to the next page you are already viewing (if you came back from there)</p><p><b>Refresh the preview</b><br>
click to refresh the page preview in the window.</p><p><b>Print the current page</b><br>
Click to print the page you are viewing.</p><p><b>Edit page in Spider</b><br>
Click to open the page you are viewing in the editor.</p><p><a name="ruler"></a><b>Show, or hide the ruler</b> <br>
Click to show or hide the ruler. You can choose which units to show on the ruler (pixels, millimeters, centimeters, inches).</p><p><b>Browser used</b> <br>
Click to select the browser used for viewing in this tab. If it is installed on the system, you can use the browser:</p><ul>
  <li>MS Internet Explorer</li>
  <li>Mozilla</li>
</ul>

<p><a name="width"></a><b>Preview area width</b><br>
Click to set the width of the preview area in pixels. Standard screen widths in Windows are available (640, 800, 1024, 1600). This property allows the page to be viewed in the target width in which it will be viewed by project guests.</p><p><b>Folder to address mapping</b><br>
Click to select or disable <a href="service_properites.php#service_properties_mappings">mapping for browsing</a> . This mapping allows pages to be viewed using a network address rather than a path on the disk. It is set separately for each project and when you change the project on the Projects tab, it automatically changes to the default mapping for this project. To find a mapping in the list, the name of the current project is marked in bold.</p></td>
