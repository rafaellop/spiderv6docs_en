<h1>Projects in general</h1>

<p>When working on multiple projects, it will be necessary to group the projects. This is what projects are used in Spider. When creating a project, you indicate a parent directory whose elements (all folders and files contained in them) will be included in the project. Thanks to this, you do not have to search the hard drive for the place where the documents of a given page are located, laboriously search or enter paths - just switch to the appropriate project.</p>

<p>You can handle projects using:</p><ul>
	<li><a href="main_tabs_myservices.php">Projects tab</a></li>
	<li><a href="main_menu_services.php">Projects menu</a> (to a limited extent)</li>
</ul>

<p>How to create and use a project read in the help for the <a href="main_tabs_myservices.php">Projects tab</a> .</p>

<h2>Benefits of using Projects</h2>

<p>Of course, you can choose not to create projects and use the Projects tab like you would with a normal file list. However, in this case, the options for projects will be inactive - the only active option will be the ability to create a new project.</p>

<p>However, creating a project allows you to:</p>
<ul>
	<li>quickly switch between your projects in <a href="main_tabs_myservices.php">Projects</a></li>
	<li>viewing the to-do tasks assigned to project documents</li>
	<li><a href="down_links_verify.php">link validation</a>, <a href="down_integrity.php"> consistency check and whole project cleanup </a></li>
	<li><a href="service_tool_synchronization.php">synchronization project content with server content</a></li>
	<li><a href="service_archive.php">archiving</a> and <a href="service_restore.php">restoring</a> projects</li>
	<li><a href="find_ext.php#find_settings">search and replace in project documents</a></li>
	<li><a href="service_map.php">creation of a graphic map of the project</a></li>
	<li>create a list of <a href="service_properites.php#servicepropertiesoftenused">frequently used project documents</a> for quick opening</li>
	<li>create an <a href="tool_htmlhelp_general.php">HTML Help</a> document from the project</li>
	<li><a href="service_properites.php#service_properties_mappings">mapping the path on your hard drive to an URL</a> in order to browse a page using an http server (useful when creating, for example, <a href="coding_php_mapping.php">pages in PHP)</a></li>
	<li><a href="service_properites.php#metaindexing">easy unification of META sections across all project documents</a></li>
   <li>easy management of <a href="main_editor_todotasks.php">to-do tasks</a> in the project and in its documents</li>
</ul>

<p>Additionally, it is possible to group projects on the list. Thanks to this possibility, it is easier to find the right project in the list, which is especially important when working on many different projects.</p>

