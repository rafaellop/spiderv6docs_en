<h1>Program settings - RSS feed settings</h1>

<!-- tre&#347;&#263; pomocy -->
<p><i> Really Simple Syndication </i> (RSS) feeds allow you to easily view the latest news from various web projects. Cream Software also provides its own RSS feeds, where you can read the latest articles in the project, or threads on the discussion forum.</p>
<p>In this category of settings, you can define the RSS feeds you want to view from the Spider's level. In addition to Cream Software's predefined channels, you can add others. Many websites provide the addresses of RSS files for their own feeds, which you can add to the list of channels available in Spider.</p>


<p><b>Add</b><br>
Click to add a new feed. A pop-up window will appear in which you need to enter the <b>Name of</b> the feed (under which you want to see the feed in the list) and the <b>Address of the RSS file</b> provided by the web project whose news you want to view.</p><p><b>Delete</b><br>
Click to remove the channel from the list.</p><p><b>Edit</b><br>
Click to edit the name of the feed or the address of the RSS file.</p><p><b>Delete all</b><br>
Click to remove all channels from the list.</p><p><b>Restore defaults</b><br>
Click to restore Spider's default RSS feeds.</p><p><b>"Clear channel cache</b><br>
The contents of the channels are stored on disk in what is known as cache memory. Click on this button to clear the cache. The next time you visit the channel, the entire content will be downloaded again.</p></td>
