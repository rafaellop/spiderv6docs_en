<h1>Toolbars - Mini toolbar</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>The mini-bar contains commands for the ongoing operation of the current document.</p>


<p><b>Closing the current document</b><br>
Click to close the current document.</p><p><b>Move to the selected document</b><br>
Click to expand the list of currently open documents. you can use this list to switch between documents.</p><p><b>View the first document</b><br>
Click to go to the first document currently open in the editor.</p><p><b>View previous document</b><br>
Click to go to the previous document currently open in the editor.</p><p><b>View next document</b><br>
Click to move to the next document currently open in the editor.</p><p><b>View the last document</b><br>
Click to go to the last document currently open in the editor.</p><p><b>Show/hide, the gutter</b><br>
Click to display, or hide <a href="main_editor.php#gutter">the editor</a>'s gutter.</p><p><b>Line numbering</b><br>
Click to show, or hide, the line numbers on the gutter.</p><p><b>Show special characters</b><br>
Click to show or hide special characters in the editor (like end of line mark, tab, etc.).</p><p><b>Line wrapping</b><br>
Click to enable line wrapping that does not fit a single line in the editor.</p><p><b>Setting up the preview document</b><br>
Click to open the tool for setting the <a href="main_preview_prevdoc.php">preview document</a>.</p><p><b>Selection of syntax coloring scheme</b><br>
Click to display a list of available coloring schemes and change the scheme for the current document. Depending on the <a href="settings_editor_syntax.php">program settings</a>, the schemes may or may not be grouped.</p><p><b>Selection of syntax coloring scheme</b><br>
Click to change to display the list of available coloring schemes and change the scheme.</p><p><b>Brackets highlighting</b><br>
Click to enable or disable <a href="settings_editor_syntax.php">highlighting of corresponding pairs of parent</a>heses.</p><p><b>Visual editing mode</b><br>
Click to enable, or disable <a href="main_editor_wysiwyg.php">the visual editing mode</a>.</p><p><b>Horizontal division</b><br>
Click to enable, or disable, the horizontal division of the <a href="main_editor.php#twoparts">editor window</a> into two parts.</p><p><b>Vertical division</b><br>
Click to enable, or disable the vertical division of the <a href="main_editor.php#twoparts">editor window</a> into two parts.</p><p><b>Automatic completion of tags</b><br>
Click to enable or disable the <a href="main_editor_codeinsight.php#tag_completion">tag auto-complete</a> function.</p><p><b>Dynamic hints</b><br>
Click to enable or disable the <a href="main_editor_codeinsight.php#code_parameter">dynamic hints</a>s feature.</p><p><b>Dynamic code</b><br>
Click to enable or disable the <a href="main_editor_codeinsight.php#code_completion">dynamic code</a> function.</p><p><b>Indent the selected text</b><br>
Click to set the indentation for the selected text.</p><p><b>Remove the indentation of the selected text</b><br>
Click to remove the indentation for the selected text.</p><p><b>Document Information </b> <br>
Click to open the <a href="tool_docinfo.php"> Info </a> window.</p><p><b>Bookmark navigator</b><br>
Click to open the <a href="tool_docinfo.php#bookmark">Bookmarks Navigator</a> window</p><p><b>Tasks to do</b><br>
Click to open the <a href="tool_docinfo.php#todotasks">Tasks-to-do</a> window</p><p><b><a href="tool_colorpalette.php">Color Palette</a></b><br>
Click to launch the color palette (default shortcut <tt> Shift + Ctrl + C </tt> ). The tool allows you to select a color from the palette, as well as take a color sample from anywhere on the screen with the help of a pipette.</p><p><b>Dynamic preview</b><br>
Click to show or hide the <a href="main_preview_dynamic.php" window>dynamic preview</a>.</p></td>
