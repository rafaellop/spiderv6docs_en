<h1>Tools - SSI+ Tags</h1>


<p>This tool is used to insert advanced SSI+ (Server Side Includes) tags into a document. They allow a wide variety of operations - from checking the date of last modification to handling large databases. Support for these tags is implemented directly on the server, but not every server supports them.</p><p>To run this tool, go to <a href="main_menu_main.php">Main Menu</a> / <a href="main_menu_tools.php">Tools</a> / <a href="main_menu_tools_objects.php">Scripts and Objects</a> / SSI+ Insertion. You will also run the described tool from the <a href="toolbars_objects_scripts.php">Java, ActiveX, SSI and other</a> toolbars.</p><p>On the left side of the window is a list of available SSI tags. After selecting any of them, possible additional parameters of the selected command appear on the right side. Pressing the "Insert" button inserts the selected tag in place of the cursor, while the "Close" button closes the window.</p><p>SSI tags have a comment format, so they are not noticed by browsers even if they are not executed by the server.</p></td>
