<h1>Toolbars - Frequently used</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>This bar contains commands for launching frequently used tools.</p>

<p><b><a href="tool_quickstart.php">Quick start</a></b><br>
Click to launch the Quick Start tool for quickly creating a page skeleton containing the most important sections.</p><p><b><a href="tool_meta.php">META section editor</a></b><br>
Click to launch the <tt>META</tt> section editor (default shortcut <tt>Shift+Ctrl+M</tt>).</p><p><b><a href="tool_body.php">BODY section editor</a></b><br>
Click to launch the <tt>BODY</tt> section editor (default shortcut <tt>Shift+Ctrl+B</tt>).</p><p><b><a href="tool_link.php">Links editor</a></b><br>
Click to launch the <tt><a></a></tt> links editor (default shortcut <tt>Shift+Ctrl+A</tt>).</p><p><b><a href="tool_graphic_insert.php">Inserting images</a></b><br>
Click to launch the image insertion tool (default shortcut <tt>Shift+Ctrl+I</tt>).</p><p><b><a href="tool_multimedia_insert.php">Embedding multimedia</a></b><br>
Click to open a tool that makes it easy to insert multimedia, such as Flash videos, RealMedia, etc., into your document.</p><p><b><a href="tool_link_map_editor.php">Links map editor</a></b><br>
Click to launch the link map editor.</p><p><b><a href="tool_hr.php">Inserting a horizontal line</a></b><br>
Click to launch the <tt>HR</tt> horizontal line insertion tool <tt>.</tt></p><p><b><a href="tool_comment.php">Inserting a comment</a></b><br>
Click to launch the comment insertion tool. The tool automatically recognises the current programming language and inserts the appropriate comment characters. Default shortcut <tt>Shift+Ctrl+K</tt>.</p><p><a href="find_text.php#find"> Find</a> <br>
Launches the search tool for the currently open document . Standard keyboard shortcut Ctrl+F.</p>

<p><a href="find_ext.php">Extended search and replace </a> <br>
Launches the Spider's most powerful search and replace tool. Using "Extended Search and Replace" you can search and replace text in the current document, all open, in all or specific Project documents or selected folders, change their character encoding, as well as check their spelling, etc.</p>

<p><b><a href="find_text.php#goto">Go to.</a></b><br>
Launches a tool that allows you to select the line, tab or other place you want to go to which you want to go to(default shortcut <tt>Ctrl+G</tt>).</p><p><b><a href="zen-coding.php">Zen-coding commands</a></b><br>
Contains commands that run the functions of the <a href="zen-coding.php">zen-coding library</a>.</p><p><b>Document navigation</b><br>
Expands the menu, which contains commands that launch tools to help you navigate through the document and orient yourself in the code. The command layout of this submenu is identical to that of the <a href="main_menu_tools_navigation.php">Main Menu / Tools / Document Navigation menu</a></p><p><b><a href="down_syntax_check.php">Check document syntax</a></b><br>
Click to validate the document syntax. The results of the syntax check will be shown in the bottom panel.</p><p><b><a href="down_links_verify.php">Check links</a></b><br>
Click to check the correctness of links in a document. The check results will be displayed in the <a href="down_links_verify.php">bottom results panel</a>. In addition, you can correct the case of the links by using the commands in the drop-down <a href="links_verify_changecaps.php">submenu</a> by clicking on the arrow to the right of the command.</p><p><b><a href="formatter_general.php">Format the source code</a></b><br>
Click to launch the source code formatting tool. You can also use it to prepare your own formatting schemes. You can also use the arrow next to the button to expand the menu containing formatting schemes (provided with Spider and created by you).</p><p><b>Additional tools</b><br>
Select the program you want to run from the submenu. You can modify the list of programs in the <a href="settings_addtools_tools.php">Program Settings - Additional Tools</a> window</p></td>
