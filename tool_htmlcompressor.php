<h1>Tools - HTML Compressor</h1>

<p>With this tool you can reduce the size of your page, so it can be loaded faster by viewers. Be careful, though, because this feature can make your document's code unreadable and even impossible to re-edit.</p><p><b>Note: Before using this tool, please test its operation on a copy of the document.</b></p><h2>Launching</h2>

<p>You can start the HTML Compressor tool from the button marked with the icon shown above. The launch button for this window is on the <a href="main_menu_tools.php"> Tools </a> / <a href="main_menu_tools_formatting.php"> Code formatting and compression </a> .</p><h2>Compression source</h2>

<p>You can perform compression on the current document, all open documents or the selected text in the text editor. Select the appropriate option.</p><h2>Compression settings</h2>

<p>The following compression options are available to remove unnecessary elements from the content of the page source code.
Select those options that should be included during the compression process. Be especially careful with the last option, which can make the document completely unreadable due to the deprivation of all line endings.</p><p><b>Redundant spaces</b><br>
Removes unnecessary spaces, e.g. double, triple, etc.</p><p><b>All tabs</b><br>
Removes tab characters</p><p><b>All comments</b><br>
Attention, dangerous for scripts and styles!</p><p><b>Tag &amp;lt;!--webbot---&amp;gt;.</b><br>
A remnant of FrontPage</p><p><b>Blank lines</b><br>
Removes unnecessary end-of-line characters</p><p><b>Unnecessary line breaks</b><br>
Removes unnecessary end of line characters</p><p><b>All end-of-line characters</b><br>
Caution: causes lack of code readability!</p><h2>Alternative</h2>

<p>An alternative to the compressor is the markup document formatting tool, which comes with a predefined setting called "Code Compression." <a href="formatter_general.php">Formatting</a> also allows you to achieve the opposite effect of compression, that is, it arranges the code nicely and legibly.</p></td>
