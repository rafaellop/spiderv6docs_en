<h1>Tools - Quick preview</h1>

<p>Quick Preview, is a tool that allows you to preview many different types of files directly from Spider, such as images, HTML documents, text files, animations, etc.</p><p>You can launch the quick view window from the menu or the file list button bar on the <a href="main_tabs_myservices.php#myservices_menubuttons"> Projects </a> tab. This command is marked with the icon shown above.</p><h2>Viewing files</h2>

<p>To preview a selected file, select it in the file list of the <a href="main_tabs_myservices.php"> Projects </a> tab, and then use the Quick View command. A window will open and you will see a preview of the selected file. In Quick View you can view HTML document files both as source code and preview in the browser, CSS style sheets (the Spider generates and displays HTML document that allows you to preview formatted), graphic files (GIF, JPG, PNG), text files (txt, css ), AVI and SWF (Flash) animations, WAV sounds and MID music.</p><h2>Window layout</h2>

<p>The window consists of a bar with buttons at the top and a preview area.</p><p><b>Button bar</b><br>
On the button bar you will find commands that allow you to both navigate through the list of files of the Projects tab (you can also move through the list using the arrow keys)and insert the viewed media into the HTML documents you are creating. The following buttons are:</p><ul>
   <li><b>Hide preview when not needed</b> - allows you to automatically close the preview window when you leave the quick preview window</li>
	<li><b>Insert into document</b> - click to insert the viewed file into the currently edited document.</li><li><b>Refresh preview</b> - reloads the same file to the preview.</li><li><b>Help</b> - displays this help page.</li><li><b>Previous and Next</b> - navigation buttons that allow you to browse through more files of the folder.</li><li><b>Preview method</b>:<ul>
		<li>for HTML document files, two buttons are displayed here that allow you to select the preview view of the document in the browser or the source code - the source code is displayed with syntax highlighting,</li><li>for text files, a button is displayed to turn word wrap on or off,</li><li>for graphic files, buttons are displayed that allow you to adjust the size of the graphic to the entire preview area or simulate the background, i.e. duplicate the graphic as it is on the background of the website,</li><li>for AVI and WAV files, a panel is displayed that allows you to play these multimedia files,</li></ul>
	</li><li><b>Open document</b> - for HTML documents allows you to quickly open for editing.</li><li><b>Document Information</b> - for HTML documents, it allows you to familiarize yourself with the detailed <a href="tool_docinfo.php">document information</a>.</li><li><b>Line wrapping</b> - enable line wrapping for previewing the source code of documents.</li></ul>

<p>The full path to the file being viewed is displayed below, and for some files, additional information is also displayed, such as for images, the dimensions of the graphic and the file size.</p>

<p><b>Preview area</b><br>
The preview area simply acts as a place where the viewed files are displayed.</p></td>
