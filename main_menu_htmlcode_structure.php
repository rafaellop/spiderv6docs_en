<p>The menu contains commands for inserting structure tags in an HTML document:</p>

<p><b>HTML tag</b><br>
Click to insert the HTML tag at the beginning of the HTML document.</p><p><b>HEAD tag</b><br>
Click to insert an HTML document header tag - HEAD.</p><p><b>TITLE tag</b><br>
Click to insert a title tag for the HTML document shown, for example, in the title bar of the browser window - TITLE</p><p><b>Tag meta name=...</b><br>
Click to insert the skeleton of a tag that allows you to enter basic information about the document usable by search engines (such as document author details, keywords, etc.) - meta name=... You can edit the document title and the content of the META tags using the <a href="tool_meta.php">Title Editor and META section</a>.</p><p><b>Tag meta http-equiv=...</b><br>
Click to insert the skeleton of a tag allowing additional information about the document to be inserted for use by HTTP servers and search engines (such as the date the document was created, the national character encoding, etc.). - meta http-equiv=...</p><p><b>Tag STYLE</b><br>
Click to insert a tag that allows CSS style sheets to be placed inside the HTML document - STYLE. It can use the <a href="main_tabs_inspector_css.php">CSS Inspector</a> on the left-hand resource panel to edit the styles.</p><p><b>SCRIPT tag</b><br>
Click to insert a tag that allows JavaScript and other scripts to be inserted inside an HTML document - SCRIPT. You can also use the <a href="tool_insert_script.php">Insert</a> Script into <a href="tool_insert_script.php">Page</a> tool to make it easier to insert script into your document.</p><p><b>LINK tag</b><br>
Click to insert a tag that allows external files, such as CSS style sheets placed in a separate file, to be attached to the document - LINK.</p><p><b>BASE tag</b><br>
Click to insert a tag that allows you to enter the base address for the HTML document - BASE.</p></td>
