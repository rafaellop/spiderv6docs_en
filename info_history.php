<h2>Spider 1.x</h2>

<p><b>November 10, 1996 - Spider 1.11 and Spider 1.12</b><br>
These were basically the same programs, however, running under Windows 3.x and Windows 95/NT, respectively. These versions are still available for download from our online archive.</p>

<p><b>August 28, 1996 - Spider 1.0 for Windows 3.x</b><br>
The first version of Spider for Windows. At this stage, Spider was a simple web page generator working on a similar principle as the DOS version, but with a mouse-operated graphical user interface.</p>

<p><b>April 4, 1996 - Spider for DOS and Linux</b><br>
A simple program that generates web pages based on answers to questions. This version worked under DOS or Linux in character mode. It was created in C language as a semester work for the Computer Science and Econometrics Department of Cracow University of Economics (now University of Economics).<!-- tre&#347;&#263; pomocy -->


