<h2>Spider 2000 4.x</h2>

<p>Spider 2000 was developed from October 27, 1998, when version 4.0 was released, over a period of about 2.5 years. During this period a total of 12 updates were released. The last one, Spider 2000 4.8.1, was released on April 12, 2001.</p>

<p>Over the course of the program's evolution, numerous features have been added, additional facilities introduced, numerous syntax highlighting schemes added, etc.</p>

