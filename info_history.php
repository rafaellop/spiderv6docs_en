<h2>Spider V6</h2>

<p>The premiere of the program takes place in 2021, 25 years after the premiere of the first version for DOS. The program is now a quarter of a century old. This is unbelievable ... <a href="info_whatsnew.php"> What's new? </a></p>

<h2>Spider 5.x</h2>

<p>In 2003, after a year and a half break from the last version of Spider 2000 (subversion 4.8.1), the eagerly awaited by users Spider 5.0 is released, the latest version 5.9.9 being released in April 2013. In the fifth version there was a division into two lines of the program:</p>
<ul>
        <li><b>Professional</b> - full version program designed for professionals. It supports advanced technologies, is richly equipped with tools to support work in different programming languages, group work, etc.</li>
        <li><b>Standard</b> - does not have many advanced tools. However, it is fully sufficient to prepare even quite advanced websites based on DHTML.</li>
</ul>
<p>Despite the profound changes that have taken place in the program, we have put a lot of effort into making sure that people using Spider 2000 can start working in the new version of the program without much trouble.</p>

<p>The first, most striking change was of course the new program interface. It was possible to customize the appearance of the program to an unprecedented extent.</p>

<p>New tabbed panels have been added to the right and bottom of the program. They contain powerful new tools. The FTP client (the "Publishing" tab known from Spider 2000) is now located on the right panel and is called "Servers".</p>

<p>The layout and content of the main menu and toolbars have changed and are now much more helpful when working. You will intuitively find all tools exactly where you should expect them. Each command has its own icon, which makes it much easier to find the needed tool among a wide range of other commands.</p>

