<h1>Program history</h1>

<p>Spider in its latest version is numbered V6. For a good few years, the program ran successfully as version 5, which already supported cutting-edge Web technologies like HTML5 and CSS3 at the time of its creation. In the few years since the release of its last version, there has been a real revolution in web technologies, especially the rise of mobile devices that now drive the world. Unfortunately, in this changing environment Pajczek became a bit mossy, and the work on version 6 dragged on mercilessly, when in the face of changes in personal life and the technological explosion, the assumptions made turned out to be too ambitious for one person who creates the program. Me. Sorry.</p>

<p>I decided that I didn't want to condemn my life's work to oblivion, however, and refresh it a bit by adding support for new technologies and adapting it at least a little to the changes that have occurred. As a result, V6 is more of an evolution than a revolution. It is now simply a good tool for everyday work with code. I personally have been using it for years not only as a page editor, but I have also managed to create several Android and iOS apps in it that have gained relative popularity and help people in their lives or work. I have also built these features into V6 so that others can benefit from my experience as well.</p>

<p>Many people brought up on Spider 5 and earlier versions, have left for other jobs, or moved on to other tools, imposed by employers or the changing market. However, I hope that the current issue will interest you not only as a curiosity from the past, but also something useful in the daily work of a front- or backend coder. I also dream that young webmasters and coders and aspiring coders will be interested in it at least enough to enter the world of coding with its help.</p>

<p>Spider has evolved from a simple page generator running in DOS character mode to its current form as a program supporting the latest technologies used on the Web.</p>
<p>Below you can see an abbreviated version history of the program in order from newest to oldest version.</p>

