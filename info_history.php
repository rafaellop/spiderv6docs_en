<h2>Spider 3.0 Light</h2>

<p>October 19, 1998 <br>
 This is a renewed version of the website generator (version 1.11 and 1.12). This time, significantly expanded, among others o FTP publishing options.</p>

<!-- nag&#322;owek podtematu -->
<h2>Spider 2.x</h2>

<p>Spider 2 would have been just a continuation of version 1.x. However, on October 19, 1998 the system crashed, and the source codes of the almost finished version were lost. This event motivated me to redefine the assumptions of the program and to restart work on version 2.0, but this time it was completely different.</p>

<p>The release of Spider 2.0 for Windows 3.x/95/NT took place on May 9, 1997. It was updated to version 2.1.2 on August 10, 1997.</p>

