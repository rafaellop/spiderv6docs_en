<h1>Main menu - Spelling</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>The commands in this menu are for spell checking, character encoding, etc. The following commands are available:</p>


<p><b>Quick spell check in the current</b><br>
Click to run a quick spell checker on the current document. The results of the spell checker will be presented in the <a href="down_find_report.php">bottom panel</a>.</p><p><b>Quick spell check in open</b><br>
Click to run a quick spell checker on all open documents. The spell check results will be presented in the <a href="down_find_report.php">bottom panel</a>.</p><p><b>Correct spelling in the current</b><br>
Click to run a spell checker on the current document. When encountering any suspicious word, you will be hinted to respond (change, zingnorn, etc.).</p><p><b>Correct spelling in open</b><br>
Click to run a spell checker on all open documents. When encountering any suspicious word, you will be hinted to respond (change, ingnore, etc.).</p><p><b>Checke the current word spelling</b> <br>
Click to spellcheck the current word in the document.</p><p><b>Highlight spelling mistakes</b><br>
Click to highlight words with errors with a red color in the editor.</p><p><b>AutoCorrect</b><br>
Click to enable or disable autocorrection.</p><p><b><a href="settings_editor_spelling.php#autocorrect">Settings</a></b><br>
Click to open the AutoCorrect Settings window.</p><hr>

<p><b>Spell-checking on a project</b><br>
Click to open the <a href="find_ext.php">Extended Search and Replace</a> window and modify settings for spell checking on a project.</p><p><b>Check spelling in folders</b><br>
Click to open the <a href="find_ext.php">Extended Search and Replace</a> window and modify  settings for spell checking in selected folders.</p><hr>

<p><b>Choose a dictionary</b><br>
Click to select the dictionary to be used by the spell checker tools.</p><p><b>Edit user dictionary</b><br>
Click to edit the user dictionary (add, delete words, etc.).</p><p><b>Activate the thesaurus</b><br>
Click to activate the thesaurus (dictionary of similar words), which allows you to select another word with a similar meaning.</p><p><b>Choose a thesaurus dictionary</b><br>
Click to select a dictionary for the thesaurus.</p><hr>

<p><b><a href="tool_typo_correct.php">Typographical correction</a></b><br>
Click to launch the typographic correction tool, which allows you to format your text so that it looks better on a page.</p><p><b><a href="tool_unicodeeditorek.php">Unicode editor</a></b><br>
Click to launch the mini editor window, which is used to paste and handle Unicode-encoded texts.</p><hr>

<p><b>Character encoding converter</b><br>
Click to open the <a href="find_ext.php">Extended Search and Replace</a> window where you can modify character encoding converter settings.</p><p><b>Change encoding in the current to...</b><br>
Click to change the encoding in the current document.</p><p><b>Read in encoding...</b> <br>
Click to reopen the document. When you reopen the document, the program will use the encoding selected from the list.</p><p><b>Save in encoding...</b> <br>
Click to save the document by changing the encoding to the selected one from the list.</p><p><b>End-of-line characters...</b> <br>
Click to expand the menu, where you can choose how to encode the end-of-line characters. Characters can be encoded as in DOS/Windows, MAC, or Unix.</p><p><b>Delete national characters in the current</b><br>
Click to replace national diacritical characters in the current document according to the rule <tt>ą</tt> - <tt>a</tt>, <tt>ć</tt> - <tt>c</tt>, etc.</p><p><b>Remove national characters in open</b><br>
Click to replace national diacritical characters in all open documents, according to the rule mentioned above.</p></td>
