<h1>Program settings - General editor settings</h1>

<!-- tre&#347;&#263; pomocy -->
<p>The general settings allow you to customize the editor in general terms, ie most of the settings that did not find their own category are in the "General" category. The "General" category is divided into two subcategories: "General settings" and "Colors of lines with tasks to be performed"</p><h2>General settings</h2>

<p>This group of settings includes settings with the following meaning:</p>

<p><b>Wrap long lines in the editor by default</b><br>
Check this option to enable line wrapping for newly opened documents. Newly opened documents are those that do not have an assigned ADD file where line wrapping is set.</p><p><b>Document buttons at the top of the screen</b><br>
Check this option to enable the display of buttons for switching documents at the top of the screen. If you disable, the document buttons will be at the bottom of the screen.</p><p><b>Shorten the title on document buttons to "n" characters</b><br>
This option causes the document title to be shortened to the number of characters indicated next to it when document titles are displayed on document selection bar buttons.</p><p><b>Show start and end of title</b><br>
Check this option to display the document title in the document button bar without the middle part</p><p><b>Highlight the current line in the editor</b><br>
Check this option to enable highlighting of the current line (the one where the cursor is) in the editor.</p><p><b>Line highlight color</b><br>
From this list, select the color you wish to use as the highlight color for the line with the cursor.</p><p><b>Line highlight frame color</b><br>
From this list, select the color you wish to use as the border color for the highlight for the line with the cursor.</p><p><b>Editor background color</b><br>
From this list, select the color you wish to use as the default background color in the editor.</p><p><b>Set the search box not to obscure the search term</b><br>
Check this option to position the search window so that it does not obscure the phrase found in the editor.</p><p><b>Display line numbers in the gutter by default</b><br>
Check this option to enable the display of line numbers in the gutter area for newly opened documents. Newly opened documents are those that do not have an ADD file assigned in which the display of line numbers for that document is set.</p><p><b>Display special characters in the editor by default</b><br>
Check this option to enable the display of special characters, e.g. TAB or space characters, for newly opened documents. Newly opened documents are those that do not have an ADD file assigned in which the display of special characters is set.</p><h2>To-do lines colors</h2>

<p>In this group of settings there are lists that allow you to choose a color for the lines where there are tasks to be performed. You can assign colors to tasks depending on their priorities. The background color list allows you to select the color you want to use as the background color for where the to-do task is marked. The text color list allows you to select the color you want to use as the text color for where to mark the to-do.</p>
<p>In addition, you can turn off the highlighting in the editor of lines containing a task to be performed (all or only those marked as completed).</p>

