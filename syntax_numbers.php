<h1>Coloring Scheme Editor - Numbers</h1>

<!-- tre&#347;&#263; pomocy -->

<p>In each block, the coloring of numbers can be enabled. This allows them to be highlighted in the text. Decimal and hexadecimal numbers can be recognised and colored differently. It is also possible to define other characters to be colored along with the number (for example, the '#' character, which denotes a hexadecimal number in CSS style sheets).</p>

<p><b>Enable number coloring</b><br>
Check this option if you wish to enable number coloring.</p>

<p><b>Decimal</b> <br>
Select from a list or create a new color style for decimal numbers.</p>

<p><b>Hexadecimal</b><br>
Allows you to select from a list or create a new coloring style for hexadecimal numbers.</p>

<h2>Special</h2>Choose where special characters can be placed in the number (in this case, not being numbers from 0-9 or letters from AF range). There are three possible variants (only one can always be active):<ul>

	<li><p><b>with prefixes</b>
- allows you to create and edit a list of special characters appearing directly before the number (without spaces). If you check the ' <b> with prefixes </b> ' option (switch on the right), the color of the prefixes will be enabled.</p></li>

	<li><p><b>with suffixes</b>
		 - allows you to create and edit a list of special characters immediately following a number (without spaces). If you select the<b>'with suffixes'</b> option (switch on the right), suffix coloring will be enabled.</p>
		<p><b>multiple allowed</b> - tick this option if you want to allow more than one suffix to be colored in a number.</p></li>

	<li><b>both</b>
- allows you to create and edit a list of pairs of special characters immediately before and after a number (without spaces). If you check the option ' <b>with suffixes and prefixes'</b> (switch to the right), the defined prefixes and suffixes will be applied.</li>
</ul>
