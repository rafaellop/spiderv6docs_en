<h1>Templates supplied with the program</h1>

<!-- tre&#347;&#263; pomocy -->


<p>Hundreds of templates are provided with the program. Those are different types of templates - from simple ones, that contain uncomplicated constructions making work in programming languages easier (e.g. function skeletons etc.), to complete document templates containing graphics etc.</p>

<p>The templates are grouped in the form of tree branches. Among them you will find, among others. branches containing:</p>

<ul>
