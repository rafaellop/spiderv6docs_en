<h1>New property or method</h1>





<p>This window allows you to add a new property or method for an object type from the object group selected in the definition file settings for scripting. <a href="tagdata_editor_tdf_scripts.php">The definition file editor</a> allows you to create and modify an existing definition of the selected scripting language, which is used in the dynamic hints functions and on the <a href="main_tabs_languages.php">Programming Languages</a> tab.</p><p>The window consists of the following fields:</p><p><b>Property name</b><br>
Enter here or modify the name of the property.</p><p><b>Type of property or method</b><br>
Check the switch corresponding to the type of property or method you are defining.</p><p><b>List of function arguments</b><br>
Enter here the list of arguments accepted by the function.
The arguments should be separated by commas, while each argument should consist of a type statement and a name (both words should be separated by a space - according to the syntax). Optional arguments can be enclosed in square brackets.</p><p><b>Result type</b><br>
Select the type of result returned by the function. You can select one of the types defined in the list of types using the <a href="tagdata_editor_tdf_scripts_objecttype.php">New Type</a> box <a href="tagdata_editor_tdf_scripts_objecttype.php">or an object type property</a>.</p><p><b>Array result type</b><br>
Select the type of array result returned by the function. You can select one of the types defined in the list of types using the <a href="tagdata_editor_tdf_scripts_objecttype.php">New Type</a> box <a href="tagdata_editor_tdf_scripts_objecttype.php">or an object type property</a>.</p><p><b>Property description</b><br>
Enter a description of the edited language property to be displayed in the dynamic tooltip and in the <a href="main_tabs_languages.php">Programming Languages</a> list.</p><p><b>Support in language versions</b><br>
Enter information about support from particular versions of the scripting language, e.g. <tt>js10</tt>.</p><p>At the very bottom of the window are additional options that allow you to specify additional properties of the method or property.</p>

