<h1>Main menu - Tools</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>The menu contains a number of submenus with various tools available in the Spider:</p>


<p><b><a href="main_menu_file_new.php">Tags wizards</a></b><br>
Contains commands to run wizards for various types of documents and specific code snippets such as tables, forms, etc. This menu is identical to the File / New Document submenu.</p><hr>

<p><b><a href="main_menu_tools_tags.php">Tags creation tools</a></b><br>
Contains commands that launch tools that allow the creation of different types of tags and whole groups of tags, such as tables, frames, forms, etc.</p><p><b><a href="main_menu_tools_structure.php">Structure</a></b><br>
Contains commands that launch tools for editing and creating basic <tt>META</tt> and <tt>BODY</tt> sections of the document.</p><p><b><a href="main_menu_tools_links.php">Links</a></b> <br>
Contains commands that launch tools for creating, editing, and verifying links in a document.</p><p><b><a href="main_menu_tools_gfx.php">Images and multimedia</a></b><br>
Contains commands that launch tools for inserting and editing tags that attach images in various formats to a document, as well as multimedia, such as Java applets, Flash movies and more.</p><p><b><a href="main_menu_tools_forms.php">Forms</a></b><br>
Contains commands that launch tools for inserting and editing forms.</p><p><b><a href="main_menu_tools_tables.php">Tables</a></b><br>
Contains commands that launch tools for inserting and editing tables.</p><p><b><a href="main_menu_tools_text.php">Text</a></b><br>
Contains commands that launch text formatting tools.</p><p><b><a href="main_menu_tools_frames.php">Frames</a></b><br>
Contains commands that launch tools for inserting and editing frames in a document.</p><p><b><a href="main_menu_tools_objects.php">Scripts and objects</a></b><br>
Contains commands that launch tools for inserting and editing tags that attach various objects, multimedia, etc. to a document, as well as for inserting <i>client-side</i> scripts (such as JavaScript).</p><hr>

<p><b><a href="main_menu_tools_verify.php">Code verification</a></b><br>
Contains commands that launch tools for verifying the correctness of syntax (various languages) in a document.</p><p><b><a href="main_menu_tools_formatting.php">Code formatting and compression</a></b><br>
Contains commands to run tools for formatting source code.</p><p><b><a href="zen-coding.php">Zen-coding commands</a></b><br>
Contains commands that run the functions of the <a href="zen-coding.php">zen-coding library</a>.</p><p><b><a href="main_menu_tools_navigation.php">Document navigation</a></b><br>
Contains commands that launch tools to help you navigate the document and orient yourself in the code.</p><hr>

<p><b><a href="main_menu_tools_other.php">Other tools</a></b><br>
Contains commands that launch other tools useful when working with the program.</p></td>
