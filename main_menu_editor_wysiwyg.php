<h1>Editor pop-up menu in visual mode</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>The <a href="main_editor_wysiwyg.php">editor's pop-up menu in visual mode</a> contains commands that run the most commonly used functions and tools in this mode:</p>

<!--

<p><b>Popraw znacznik w narz&#281;dziu</b><br />
Kliknij, aby otworzy&#263; narz&#281;dzie do edycji znacznika, na kt&oacute;rym stoi kursor (domy&#347;lny skr&oacute;t <tt>F5</tt>).
