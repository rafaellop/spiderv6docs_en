<h1>Program settings - User and version check</h1>

<!-- tre&#347;&#263; pomocy -->
<p>The category named <b> User </b> contains settings that allow you to define the user of the program. You can enter the username and email address (your data), which will identify him, for example, as the author in document information files, in the META section or when blocking files.
 <br><br>
The second group of settings here is <b> Version Check </b> . In this group, you can enable or disable the function of automatic checking for the latest version, or define the time between successive connections to verify that the version is up-to-date. This could be checking: <br></p><ul>
<li>at each time the program is started</li>
<li>once a day</li>
<li>once a week</li>
<li>once a month</li>
</ul><a name="settings_newsinfo"></a>
<br>
The third group of settings is <b>Cream Software News Information</b>. The settings found here determine how information about new articles published on the <a href="https://www.creamsoft.com/blog/" target="_blank">Cream Software blog</a> site is updated and displayed. This information appears at the bottom of the screen when the program detects new articles. Checking is done by default 2x a week, and the window only appears when there is something interesting to show. You can also check for this news manually from the <a href="main_menu_help.php">Help</a> menu, Cream Software News Information command.</td>
