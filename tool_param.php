<h1>Tools - Object parameter</h1>



<p>Using this tool, you can define the start parameters of any object embedded in a Web page (Java applet, ActiveX control, etc.).</p><p>To run this tool go to <a href="main_menu_main.php">Main Menu</a> / <a href="main_menu_tools.php">Tools</a> / <a href="main_menu_tools_objects.php">Scripts and Objects</a> / Object Parameter. You will also run the described tool from the <a href="toolbars_objects_scripts.php">Java, ActiveX, SSI and other</a> toolbars.</p><p>In the window you have access to the following settings:</p><p><b>Parameter name</b><br>
Enter the name of the parameter in this field.</p><p><b>The value of the parameter</b><br>
Enter the value of the parameter in this field, or select a file using the button next to it if you want to specify it as a parameter.</p><p><b>Type of parameter</b><br>
Use this list to specify the type of parameter.</p><p><b>File type</b><br>
From this list you can select the file type if the passed parameter value points to a file. You select the file using the button above. You can also specify the URL to the file.</p><p>Tip: If you insert an object into the page using the <a href="tool_objects.php">Insert Object</a> tool, you can specify the applet's starting parameters directly in the tool's window - on its second tab.</p></td>
