<h1>Main Menu - Edit/Selected text to...</h1>

<!-- Tre&#347;&#263; pomocy -->

<p>This menu allows you to quickly change the format of selected text, create a list, table, etc. The following commands are available:</p>

<p><b>Selected to the covered with style</b><br>
Click to tag the selected text with a SPAN or DIV tag (depending on the setting in the <a href="settings_codeinsight_additional.php#span_div">Program Settings / Other Settings / Eases ... / Other Facilitations) window</a> with the CSS style.</p><p><b>Selected to a class</b><br>
Click to tag the selected text with a SPAN or div tag (depending on the setting in the <a href="settings_codeinsight_additional.php#span_div">Program Settings / Other Settings / Facilitations ... / Other Facilitations) window</a> with the CSS class.</p><p><b><a href="tool_lists.php">Selected to a numbered list</a></b><br>
Click to create a numbered list from the selected text. Each line of selected text will be a new item in the list.</p><p><b><a href="tool_lists.php">Selected to a unnumbered list</a></b><br>
Click to create an unnumbered list from the selected text. Each line of selected text will be a new item in the list.</p><p><b>Selected to a line with the &amp;lt;br&amp;gt; endings</b><br>
Click to place the line break tag &amp;lt;br&amp;gt; at the end of each line of selected text.</p><p><b>Selected to embraced with &amp;&amp;quot;;</b> <br>
Click to enclose the selected text with special characters <tt>&amp;&amp;quot;;</tt> (the entity representing the quotation marks).</p><p><b><a href="tool_comment.php">Selected to a comment</a></b><br>
Click to create a comment from the selected text. The tool automatically recognizes the language used and applies the appropriate comment characters.</p><p><b>Selected to a bordered text</b><br>
Click to include the text in a table with a border.</p><p><b><a href="tool_text2table.php">Selected to a table</a></b><br>
Click to convert the selected text to a table. The tool allows you to define table layout, styles, etc.</p><p><a href="tool_embrace.php">Include or remove cover</a><br>
Click to open the tool that allows you to include the selected text in the specified text. It is also possible to remove the inclusion of comment characters, etc.</p><p><b>Duplicate selection</b><br>
Click to insert an exact copy of the selected text immediately after the selected text.</p><hr>

<p><!--  --><b>Replace email addresses with clickable links</b><br>
Click to change all e-mail addresses in the selected text to links to send mail (<tt>A</tt> with the appropriate <tt>href</tt> attribute).</p><p><!--  --><b>Replace URLs with clickable links</b><br>
Click to change all URLs in the selected text to links (<tt>A</tt> with the appropriate <tt>href</tt> attribute).</p><hr>

<p><b>Script formatting</b><br>
Click to expand the submenu containing commands for formatting the selected text so that it can be correctly used in scripts (e.g. JavaScript, PHP and others) to display it in the document.</p><hr>

<p><b><a href="macrocodes.php">Selected to a template</a></b><br>
Click to create a new template based on the selected text.</p><p><b>Selected to lowercase</b><br>
Click to change all letters in the selected text to lowercase.</p><p><b>Selected to UPPERCASE</b><br>
Click to change all letters in the selected text to UPPERcase.</p><p><b>Selected to reverse-case</b><br>
Click to change all lowercase letters in the selected text to UPPERcase and at the same time UPPERcase to LOWERcase. This function is especially useful when you have entered text and at the same time unintentionally pressed the CapsLock key.</p><p><b>Selected to As in sentence</b><br>
Click to change the first letter of the selected text to uppercase and the rest to lowercase.</p><p><b>Selected to As In Title</b><br>
Click to change the first letter of each word to uppercase, the rest of the letters to lowercase.</p><p><b>Selected to the next lettercase proposal</b> <br>
Click to apply consecutive case-sensitive transformations to the selected text. It is best to use the function using the default keyboard shortcut <tt>Shift+F3</tt>.</p><hr>

<p><b>Selected to sorted (A-Z)</b><br>
Click to sort the lines of the selected text in alphabetical order.</p><p><b>Selected to sorted (Z-A)</b><br>
Click to sort the lines of the selected text in reverse order.</p><p><b>Remove HTML tags in the selected</b> <br>
Click to remove html tags from the selected text.</p><p><b>Delete blank rows in selected</b><br>
Click to remove all blank lines from the selected text.</p><p><b>Replace "with \"</b><br>
Click to replace all " to \ " characters in the selected text.</p><p><b>Replace 'with \'</b><br>
Click to replace all ' with \' characters in the selected text.</p><p><b>Replace &amp;lt; and &amp;gt; with &amp;lt; and &amp;gt; in the selected</b><br>
Click to replace the &amp;lt; and &amp;gt; characters in the selected text with entities &amp;lt; and &amp;gt;. This makes it possible to display them in the document, most often they are used to present HTML tags on the page that you do not want to interpret.</p><p><b>Replace &amp;lt; and &amp;gt; with &amp;lt; and &amp;gt; in the selected</b><br>
Click to replace entities &amp;lt; in the selected text and &amp;gt; on &amp;lt; and &amp;gt;.</p></td>
