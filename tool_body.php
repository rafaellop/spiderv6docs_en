<h1>Tools - BODY Settings</h1>

<p>The BODY section settings window allows you to specify properties for the <tt>BODY</tt> tag <tt>.</tt> This tag determines the appearance of the part of the page that displays information. Therefore, the settings of this section often affect the appearance of the document. However, while this window offers many settings, many of them are obsolete. It is now recommended to use <a href="coding_css.php">CSS styles</a> to determine the appearance of the document. Nonetheless, the described tool can sometimes come in handy.</p><p>You will launch the BODY Section Settings window from the <a href="main_menu_main.php">Main Menu</a> / <a href="main_menu_tools.php">Tools</a> / <a href="main_menu_tools_structure.php">Structure</a> /  Section Editor, as well as from the <a href="toolbars_often.php">Frequently Used</a> toolbar and the <tt>Shift+Ctrl+B</tt> shortcut.</p><p>To quickly insert a background into a <tt>BODY</tt> tag in a document opened in the editor, drag an image from the file list into it. The "BODY Settings" tool will open automatically and the appropriate path to the image will be entered immediately.</p>
   
<h2>Background and color settings - obsolete</h2>

<p>The first part of the window allows you to specify the highlighting scheme and background graphic underlay for the document. All these settings have a <i>deprecated</i> status and it is not recommended to use them. It is recommended to use <a href="coding_css.php">CSS styles</a>. Here you will find the following settings:</p><p><b>Background</b> <br>
Check to select the background image for your document.</p><p><b>Path to background artwork</b> <br>
Specifies the path to the background image file for this page. By pressing the button next to it, you can select an image with the help of a dialog box.</p><p><b>Background color </b><br>
Check to select the background color for the document.</p><p><b>Text color </b><br>
Check to select the color of the text in the document.</p><p><b>Color of links </b><br>
Check to select the color of the links in the document.</p><p><b>Color of active links </b><br>
Check to select the color of the links that the browser links to.</p><p><b>Color of visited links </b><br>
Select to select a color for links that have already been visited.</p><p><b>Simplified preview</b><br>
Here you will see a preview of the graphic used as the background of the web page along with a preview of the text colors. This makes it easier for you to judge how a particular text color matches the background used.</p><h2>MS Internet Explorer margin settings</h2>

<p>The settings in this group allow you to specify margin settings. Unfortunately, these settings only work in MS Internet Explorer so here too it is recommended to use <a href="coding_css.php">CSS styles</a>. You will find the following settings here:</p><p><b>Internet Explorer margins</b><br>
A set of four switches and four fields in which it specifies the distance of the page content from the browser's left, right, top and bottom edges.</p><p><b>Watermark in Internet Explorer</b><br>
Enabling the watermark establishes a fixed, non-shifting, page background.</p><h2>Netscape margin settings</h2>

<p>The settings in this group allow you to specify margin settings. Unfortunately, these settings only work in the Netscape browser so here too it is recommended to use <a href="coding_css.php">CSS styles</a>. Here you will find two switches and two fields responsible for margin width (left and right) and height (top and bottom).</p><h2>Element style</h2>

<p>This group allows you to specify the style for the tag. Here you will find three fields that allow you to specify:</p><p><b>Style definition</b> <br>
In this field you define the style for the element placed in the document. The button next to it launches the <a href="tool_style_editor.php">style editor </a> tool to create a style definition in a user-friendly and convenient way. By clicking there you can also modify an existing style.</p><p><b>Class selection</b><br>
In this field you specify the class identifier assigned to the tag. The drop-down list allows you to access classes defined in the current document and in external style sheets attached to it. The button next to it launches the <a href="tool_style_class.php">tool for creating a new class</a>.</p><p><b>Item ID</b><br>
Here you provide a unique identifier for this element in the document. These identifiers can be used, for example, when making jumps to specific parts of the page or in scripts.</p><h2>"Events and Attributes" button</h2>

<p>The "Events and Attributes" button allows you to open the <a href="tool_events_attributes.php">Events and Other At</a>tributes window. This window allows you to specify additional attributes of the edited tag that do not have their counterparts in the tool window. There you can also specify events for scripting languages that allow you to achieve various special effects.</p></td>
